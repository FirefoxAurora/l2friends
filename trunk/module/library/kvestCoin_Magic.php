Let's Become a Royal Member (требуется уровень 55+)
<br>

<br>Чтобы получить этот квест, ты должен уже быть Нумизматом 1 уровня
(level 1 Coin Collector). Поговори с Завскладом в Hunter Village
(Warehouse Keeper Sorint). Он скажет, что ему нужны две вещи. Coin
collecting album и Kail's coin. Порядок, в котором ты добудешь эти
вещи, не важен.
<br>

<br>
Альбом для Монет (Coin Collecting Album) продаётся у Jeweller Sandra в Giran Castle Town. Однако,
<br>она торгует им один раз за подход. Когда кто-то купит его, он
исчезает и появится снова примерно через 4 часа. Альбом появится в
твоём обычном инвентаре, но ты не сможешь обменять, продать его или
положить на склад, так что подумай хорошенько каким персонажем ты
покупаешь этот альбом. По окончании прохождения квеста альбом исчезнет.
<br>

<br>
Kail's Coin выбивается из Ancient Gargoyles на Forsaken Plains. При выбивании в пати ты должен нанести удар последним.
<br>

<br>Неси два предмета обратно Sorint. Ты получил членство в Королевском
Клубе (Royal Membership), теперь ты Королевский Нумизмат, а это значит,
что можно брать Kail's Magic Coin Quest.
<br>

<br>

