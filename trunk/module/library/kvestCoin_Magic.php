Kail's Magic Coin (требуется уровень 55+)
<br>

<br>Сначала ты должен стать Королевским Нумизматом. Когда ты уже
Королевский Нумизмат, поговори с Head Blacksmith Vergara в Hunter's
Village на предмет квеста.
<br>Он предложит поохотиться в Forsaken Plains на Fallen Orcs, Fallen
Orc Shaman, Fallen Orc Captains и Fallen Orc Archers, чтобы добыть
Kail's Coins. Монетки падают следующим образом:
<br>

<br>
Fallen Orcs: Kail's Silver Basilisk
<br>
Fallen Orc Archers: Kail's Gold Golem
<br>
Fallen Orc Shamans: Kail's Blood Dragon
<br>
Fallen Orc Captains: случайно выбрасывает Kail's Silver Basilisk, Gold Golem или Blood Dragon
<br>

<br>Каждый рецепт A-grade требует по 10 монет каждого образца.
Вернувшись к Vergara ты увидишь, что понадобятся и монетки из обычного
квеста на монеты.
<br>

<br>
1 Blood Basilisk = 100 Blood Medusas
<br>
1 Silver Dryad = 100 Silver Unicorns 
<br>
1 Gold Giant = 100 Gold Wyverns
<br>

<br>В общем, низший A-grade потребует 2 Silver Dryads, а высший A-grade
потребует либо 3 Blood Basilisks, либо 3 Gold Giants. И все забирают по
10 каждого сорта Kail's Coins.
