Coin of Magic (требуется уровень 40+) 
<br>

<br>
1. Поговорите с Warehouse Keeper Sorint. 
<br>

<br>
2. Поговорите с Hunters Union President Benard.
<br>

<br>
3. Идите охотиться на Harit Lizardman пока не получите Kaldis Gold
Dragon (появится в квестовых вещах). Они появляются южнее Forest of
Mirrors, лучше телепорт из Hunter's Village в Front of Anghel Waterfall
и отбежать немного запад. (Убивать можно любых Lizardmen’ов из
семейства Harit).
<br>

<br>
4. Возвращайтесь к Warehouse Keeper Sorint и вступите в Coin Collecting Guild. Вы будете членом гильдии 3го уровня.
<br>

<br>5. Собирайте монеты с монстров. Монетами, которые вы будете
получать с монстров, можно обмениваться, торговать, в общем, делать все
что угодно. Чтобы вы могли получать более ценные монеты, и получать
более ценные вещи вам нужно повысить ваш уровень в гильдии.
<br>

<br>
С кого монеты можно добыть
<br>Монеты падают с нижеуказанных мобов, не зависимо от уровня
персонажа, возможно список не полный, просто привожу тех мобов которые
заявлены в большинстве описаний и с которых я лично выбивал.
<br>

<br>Blood Medusa выбиваются из следующих монстров: Lakin, Timak Orc
Shaman, Timak Orc Soldier, Timak Orc Archer, Punishment of Undead.
<br>

<br>Silver Unicorn выбиваются из следующих монстров: Formor, Formor
Elder, Vanor Silenos Shaman, Oel Mahum, Oel Mahum Warrior, Tarlk
Bugbear High Warrior.
<br>

<br>Gold Wyvern выбиваются из следующих монстров: Timak Orc, Hatar
Hanishee, Royal Cave Servant, Shackle, Malruk Succubus Turen, Headless
Knight.
