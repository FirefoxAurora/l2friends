      <p> <strong>1. Технические вопросы.</strong></p>      
      <p><strong><a name="1_1"></a>1.1. Как начать играть на сервере? </strong><br>
  Зарегистрируйтесь  и скачайте необходимые для игры файлы.<br>
  Сразу после этого вы можете играть на сервере под введенными в регистрационной форме именем и паролем.</p>
      <p align="center">        [<a href="#1">обратно наверх</a>]</p>            <p> <strong><a name="1_2"></a>1.2. Я сделал как написано в п.1.1., но заходить не хочет... </strong><br>
Возможны 2 варианта:</p>
      <ul>
        <li> Сервер по какой-либо причине в данный момент не работает. Ппопробуйте зайти попозже, когда сервер запустят. </li>
        <li> Такое имя уже кто-то использует - попробуйте ввести другое.
</li>
      </ul>      
      <p align="center">[<a href="#1">обратно наверх</a>]                             </p>            <p><strong><a name="1_3"></a>1.3. Когда я ввожу имя и пароль и пытаюсь зайти - мне пишут что аккаунт уже используется. </strong><br>
  Да, иногда бывает такое при неправильном выходе из игры. Обычно через 15-20 минут все впорядке и вы вновь можете войти в игру. </p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>
      <p><strong>2. Управление в игре.</strong></p>
      <p><strong><a name="2_1"></a>2.1. Как передвигаться?</strong><br>
        Для передвижения в какую-то точку - нажмите на ней левой кнопкой мыши. Персонаж сам пойдет в ту точку, если не обнаружит никакого препятствия.</p>      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
        <p><strong><a name="2_2"></a>2.2. Можно ли передвигаться с помощью клавиатуры?</strong><br>
        Да, с помощью стрелочек.<br>
        Вверх/вниз - персонаж побежит вперед/назад(относительно камеры обзора).<br>
        Влево/вправо - повернет камеру обзора влево/вправо.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_3"></a>2.3. Как атаковать моба?</strong><br>
        Вы должны сначала выделить его, нажатием левой кнопки мыши, а потом повторным нажатием - атаковать.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_4"></a>2.4. Сделал все как в п.2.3. - не атакует.</strong><br>
        Возможно вы пытались атаковать не враждебного моба, не стоит таких атаковать. Но для принудительной атаки всегда есть комбинация CTRL+левая кнопка мыши.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>                  
      <p><strong><a name="2_5"></a>2.5. Как открыть сумку(инвентарь)?</strong><br>
        Нажмите TAB.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_6"></a>2.6. Как посмотреть информацию о вещи?</strong><br>
        Один раз нажмите на ней в инвентвре левой кнопкой мыши. Появиться небольшое окошко с описанием и некоторыми параметрами.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_7"></a>2.7. На одеть/снять или использовать вещь?</strong><br>
        Двойное нажатие на ней левой кнопкой мыши когда вещь находиться в инвентаре/на вас.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_8"></a>2.8. Как посмотреть мои основные параметры?</strong><br>
        Нажмите в игре Alt+T.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_9"></a>2.9. Как посмотреть какие скиллы/магии у меня сейчас выучены?<br>
      </strong>Нажмите в игре Alt+K. Там будут 2 закладки Active и Passive.<br>
        Pasive - это то что применять не надо, действует всегда (возможно токо в определенных условиях).<br>
        Active - это те скиллы и магии что надо применять вручную каждый раз. Предже чем вы сможете их использовать снова - пройдет некоторое время - они должны восстановиться. Это время в основном зависит от скилла.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_10" id="2_10"></a>2.10. Как получить доступ к различным действиям (поднять предмет, атаковать и пр)?</strong><br>
        Нажмите Alt+C, 1я группа иконок - то что вам нужно.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_11"></a>2.11. Как организовать партию(party)?</strong><br>
        Нажмите Alt+C, поэкспериментируйте со значками 2й группы.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            
      <p><strong><a name="2_12"></a>2.12. Как получить доступ к &quot;жестам&quot; (приветствие, разминка, победа и пр.)?</strong><br>
        Нажмите Alt+C, 3я группа иконок - то что вам нужно.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>      
      <p><strong><a name="2_13"></a>2.13. Как получить доступ к системному меню (настройки, выход и пр.)?</strong><br>
        Нажмите в игре Alt+X.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>      
      <p><strong><a name="2_14"></a>2.14. Как использовать скилл/магию на мобе?</strong><br>
        Выделите его нажатием левой кнопки мыши, затем нажмите на иконку скилла/магии. Персонаж подбежит на нужное расстояние и использует скилл/магию.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>      
      <p><strong><a name="2_15"></a>2.15. Все это хорошо, но ужасно неудобно каждый раз копаться в каких-то меню, искать скиллы, действия и пр.</strong><br>
        Каждый раз вовсе необязательно - для это в игре есть панель для быстрых действий (полоска с пустыми слотами(ячейками)) она может быть горизонтальной и вертикальной - туда можно перетажить вещь, скилл/магию, действие, в общем почти все для их быстрого использования.<br>
        Использльвать можно нажатим левой кнопкой мыши на значке на этой панели или клавишами F1-F10.<br>
        Если вам не хватает ячеек - вы можете перелистнуть на следущую страницу и заполнить ее (Alt+F1-F10 - для переключения между страницами).</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>      
      <p><strong><a name="2_16"></a>2.16. Как купить, телепортироваться, взять квест и т.п.?</strong><br>
        Выделите торговца, гэйткипера и т.п. нажав на него левой кнопкой мыши. Повторное нажатие чтоб заговорить. Появиться диалоговое окно, где будет синим выделено несколько пунктов, которые можно будет выбрать нажатием - это вам и нужно.</p>
      <p align="center">[<a href="#1">обратно наверх</a>] </p>            <p><strong>3. Чат. </strong></p>
      <p><strong><a name="3_1"></a>3.1. Как что-то сказать игроку который находиться рядом со мной? </strong><br>
    Просто введите текст в консоли и нажмите Enter. Над вашей головой появиться ваше сообщение. А так же в консоли у вас и у всех находящихся поблизости игроков отобразиться БЕЛЫМ цветом ваше сообщение. </p>      <p align="center">[<a href="#1">обратно наверх</a>]</p>
      <p>        <strong><a name="3_2"></a>3.2. Сделал как в п.3.1., но сообщение почему-то не белого цвета. </strong><br>
      Наверно вы находились не в закладке All консоли - тогда ваше сообщение могло не дойти до адресата или его услышал кто-то лишний. Подробнее ниже. </p>
      <p align="center">[<a href="#1">обратно наверх</a>]</p>
      <p>        <strong><a name="3_3"></a>3.3. Как послать сообщение всем? </strong><br>
        Поставьте перед ним "!" - восклицательный знак. Это сообщение отобразиться оранжевым цветом в консоли. <br>
      Игровое пространство разделено на зоны и вас услышат те кто будет в одной зоне с вами. </p>
      <p align="center">[<a href="#1">обратно наверх</a>]</p>
      <p>        <strong><a name="3_4"></a>3.4. Как послать приватное сообщение? </strong><br>
        Напишите "кому текст <br>
        Пример: нодо послать персонажу <strong>Nick</strong> сообщения &quot;привет&quot;<br>
      пишите "<strong>Nick</strong> привет </p>
      <p align="center">[<a href="#1">обратно наверх</a>]</p>
      <p>        <strong><a name="3_5"></a>3.5. Где лучше всего делать предложения о покупке/продаже? </strong><br>
        Либо на форуме, либо в игре есть канал Trade - переключитесь на него в консоле или просто поставьте "+" перед вашим сообщением - оно отобразиться розовым цветом. <br>
        Замечание: Не пишите ничего лишнего в торговом канале. <br>
      Замечание: Торговый канал как и общий чат("!") работает в пределах зоны, так что эффективнее поместить объявление на форум.</p>
      <p align="center">[<a href="#1">обратно наверх</a>]</p>
      <p>        <strong><a name="3_6"></a>3.6. Я в партии(party) как мне отослать сообщение своим сопартийцам? </strong><br>
      Преключитесь на канал Party или просто перед сообщениями ставьте "#" - они будут отображаться зеленым цветом. </p>
      <p align="center">[<a href="#1">обратно наверх</a>]</p>
      <p>        <strong><a name="3_7"></a>3.7. Я в клане, как мне послать сообщение соклановцам? </strong><br>
    Преключитесь на канал Clan или просто перед сообщениями ставьте "@" - они будут отображаться синим цветом.</p>
