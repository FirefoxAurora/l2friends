    <p> <strong>4. Первый раз в игре.</strong><br>
      <br>
      <strong><a name="4_1"></a>4.1. Я первый раз в игре. Что мне делать? </strong><br>
Оденьтесь - вам дана начальная экипировка, не высшего класса, но поначалу поможет. <br>
Недалеко от места появления должны обитать слабые мобы - Keltir'ы, Wolf'ы и т.п. Отойдя подальше вы обнаружите более сильных мобов. Убивайте тех, уровень которых примерно такой же что и у вас, до тех пор пока не получите 5й(для воина) или 7й (для мага) уровни. <br>
Идите в город(он тоже рядом).. Найдите Master'а и поговорите с ним - выберите Learn Skills и в зависимости от того кого вы качаете - выучите нужные вам скиллы на которые хватит SP. <br>
Найдите магический магазин - там вы сможете купить карту(к этому времени у вас накориться нужная сумма). <br>
Вместо покупки карты - вы можете взять на нее квест - у одного из НПС в месте важего появления... Выполнение его займет совсем мало времени даст карту столь необходимую для нормальной ориентировки в мире. <br>
Теперь вы можете ориентироваться в мире... </p>
    <p align="center">[<a href="#1">обратно наверх</a>]</p>
    <p>        <strong><a name="4_2"></a>4.2. Что такое SP? </strong><br>
      SP=Skill Points. За убийство мобов вы получаете не только опыт, но и SP. SP вы можете потратить на приобретение какого-либо скилла и еще на некоторые вещи (например повышение уровня гильдии).</p>
    <p align="center">[<a href="#1">обратно наверх</a>]</p>
    <p>        <strong><a name="4_3"></a>4.3. Зачем нужны Spellbook'и? </strong><br>
      Чтобы выучить какую-либо магию нужно будет иметь при себе соответствующий spellbook. ростейшие спеллбуки можно купить в магазине... Остальные придется выбивать или приобретать у игроков. </p>
    <p align="center">[<a href="#1">обратно наверх</a>]</p>
    <p>        <strong><a name="4_4"></a>4.4. Как скролить карту? </strong><br>
      Включите ее, нажмите на ней левой кнопкой мыши и не отпуская перетащите... Карта переместиться в нужную вам сторону - так вы можете простомотреть что находиться в самом дальнем ее углу. </p>
    <p align="center">[<a href="#1">обратно наверх</a>]</p>
    <p>        <strong><a name="4_5"></a>4.5. Я купил карту, но не пойму где Дион и другие города о которых все говорят. </strong><br>
        Дело в том что часть рас появляются в другом мире - Elmore и если вы начали там, то купили карту этого мира. Вам надо еще немного подкачаться в Элморе, а после токо как вы поднакопить чуток деньжат - телепортироваться в другой мир. <br>
      Если вы начали человеком, то находитесь в основном мире, но на острове - с него совсем не обязательно телепортироваться - можно пробежать по дну - пока это не повредит вашему здоровью. </p>
    <p align="center">[<a href="#1">обратно наверх</a>]</p>
    <p>        <strong><a name="4_6"></a>4.6. Как телепортироваться? </strong><br>
      Для этого поговорите с Gatekeeper'ом... Он перечислит куда может вас телепортировать и за сколько. Gatekeeper'ы есть по всех городах. </p>
    <p align="center">[<a href="#1">обратно наверх</a>]</p>
    <p>        <strong><a name="4_7"></a>4.7. Что-то случилось - все вокруг бело-голубое... </strong><br>
      Вы провалились под землю, иногда помогает рестарт игры, а если нет, то пишите на форум в раздел проблема с чарами или если в игре есть ГМ - приват ему. </p>
    <p align="center">[<a href="#1">обратно наверх</a>]</p>
