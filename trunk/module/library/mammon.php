<h3 align="left">Как производится Обмен или улучшение оружия</h3>
<left><br />
  <img src="ugol2000.gif" width="4" height="8" /> Обмен Оружия<br />
  <br />
  Технология проста. Если требуется поменять оружие одного класса - заходим в "обмен оружия" (обязательно положите его в инвентарь) выберите сначала своё - потом на какое меняем. <br />
  При Обмене Оружия обязательно выбирать оружие внутри группы (то-есть поменять например Saber - 1 группа на Artisan's Sword 2 группа в обмене оружия мы не сможем, это делается в пункте улучшения оружия)<br />
  <br />
  <img src="ugol2000.gif" width="4" height="8" /> Улучшение Оружия<br />
  <br />
  Аналогично но требуется энное количество "Ancient Adenа". То-есть заходим в "улучшение оружия" выбираем свое а потом выбираем оружие из следующей группы (нельзя перескакивать через группу - только на одну группу выше). <br />
  Например, чтобы получить из Crystal Staff (6 группа)- Sage Staff (8 группа) меняем в следующем порядке Crystal Staff (6) на Cursed Staff (7) потом на Sage Staff (8).<br />
  <br />
  <br />
  <br />
</left>
