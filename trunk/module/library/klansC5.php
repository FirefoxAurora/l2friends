<p><br><b>Академия</b><br>Кланы 5 уровня и выше могут создать Академию,
которая поможет новичкам освоиться в игре, и ускорить прокачку
низкоуровневых персонажей. В Академию можно принять любого персонажа,
не достигшего 40 уровня и не получившего 2- ю профессию. Максимальное
количество персонажей в Академии – 20. <br>Создать Академию может лидер клана с помощью NPC, отвечающего за клановую активность (High Priest, Grand Master и т.д.)<br>Когда
персонаж, входящий в Академию, достигает 40 уровня и получает 2- ю
профессию, он получает специальную вещь на память у того NPC, у
которого он получит 2- ю профессию. После этого персонаж будет исключен
из Академии. При этом персонажа могут незамедлительно принять в
основной клан или отряд/орден (то есть не будет штрафа, как это бывает
при выходе из клана).<br>Персонажи, входящие в Академию клана, являются
полноправными членами клана за исключением отдельных ситуаций. Название
клана персонажа, входящего в состав Академии отображается желтым
цветом. Члены Академии могут общаться в чате клана и в чате альянса.<br>При
приеме персонажа в Академию клана, он автоматически получает доступ к
замку и клан холлу, которыми владеет клан, наравне с членами основного
клана, отрядов и орденов. Также члены Академии получают доступ к
клановому хранилищу и доске объявлений. Однако лидер клана может
установить запрет для всех членов Академии на использование
вышеперечисленных возможностей.<br>Следующие привилегии не могут быть даны членам Академии:</p>
<ul type="square">
<li>Принятие или исключение из клана. 
</li><li>Назначение титулов, изменение символа клана, назначение глав
отрядов и орденов и наставников, повышение уровня клана,
администрирование доски объявлений. </li><li>Объявление войны, роспуск клана, дополнительные функции. 
</li><li>Участие в аукционе, управление налогами, регистрация на осаду, управление наемниками. </li></ul>
<p>Во время войны с другими клана или во время осады, к членам Академии применяются следующие правила:</p>
<ul type="square">
<li>Члены Академии теряют обычное количество опыта, даже если клан ведет двустороннюю войну. 
</li><li>Члены Академии не видят значка на других персонажах, обозначающего враждебный клан 
</li><li>Члены Академии не являются союзниками во время войны или во время осады. </li></ul>
<p><b>Наставники.</b><br>Члены Основного клана, в чьи привилегии входит
назначение глав отрядов и орденов и наставников, могут назначить членов
основного клана и членов отрядов и орденов наставником члена Академии.
Также связь между новичком и наставником может быть разорвана.<br>Имена
членов Академии, к которым пристален наставник, отображаются желтым
цветом. Имена персонажей без наставника отображаются белым. К каждому
новичку в Академии может быть назначен только один наставник. При входе
в игру наставника или новичка, у обоих будет отображаться
соответствующее системное сообщение. <br>С помощью наставника новичок
может выполнить специальное квест на получение специального набора
брони D-класса. Этот набор брони могут носить только члены и выпускники
Академии. Тем не менее, если персонаж будет исключен из Академии до
момента формального выпуска, то броня не будет снята с персонажа,
однако, однажды сняв (после исключения из Академии), ее уже нельзя
будет одеть вновь.<br><br><b>Назначение привилегий членов клана.</b><br>В
связи с расширением возможностей лидера-клана, список полномочий,
которые лидер может делегировать другим членам клана, существенно
обновлен. Лидер клана должен разумно раздавать привилегии членам клана.<br>Новые привилегии, которые лидер клана может делегировать:</p>
<ul type="square">
<li>Исключение из клана. 
</li><li>Назначение глав отрядов и орденов и наставников. 
</li><li>Присвоение званий. 
</li><li>Управление замком и манором. </li></ul>
<p>Привилегии, которые лидер клана может делегировать: </p>
<ul type="square">
<li>Управление символом и эмблемой клана: смена и удаление эмблемы и символа клана. 
</li><li>Использование возможностей клан холла. 
</li><li>Управление клан холлом. 
</li><li>Управление замком: манор, налоги, наемники. 
</li><li>Модернизация замка: усиление ворот и стен, установка ловушек. </li></ul>
<p>Лидер клана может делегировать даже те привилегии, которые клану в данный момент не доступны (у клана нет клан холла, замка). <br>Также
можно настроить автоматическое делегирование полномочий в зависимости
от звания члена клана. Для этого нужно воспользоваться кнопкой «Edit
Privileges», отредактировав соответствующие записи. Затем необходимо
выбрать члена клана, и нажав кнопку «Change Rank», установить
соответствующее звание.<br><br><b>Смена лидера клана.</b><br>Лидер
клана может назначить преемника. Для этого лидеру клана нужно посетить
NPC, отвечающего за клановую деятельность (High Priest, Grand Master и
т.п.), и назначить нового лидера клана. Смена лидера клана произойдет
во время очередных профилактических работ. Отменить смену лидера клана
можно в любой момент до начала профилактических работ.<br><br><b>Окно клана.</b><br>Интерфейс
окна клана был изменен в соответствии с появившимися возможностями –
появились дополнительные функции, структура клана стала более
прозрачной. Чтобы назначать привилегии члену клана нет необходимости,
чтобы он был в игре.<br>При двойном щелчке по имени члена клана
откроется окно управления. В этом окне можно изменить титул персонажа,
его звание и привилегии. В этом же окне можно назначить игроку
наставника (если он в Академии). Также можно исключить игрока из клана
или перевести его в один из отрядов или орденов.</p><img alt="" src="module/library/IMG/clan_window_c5.jpg"> <br>
<p>В окне клана был добавлен выпадающий список, с помощью которого
можно просмотреть список персонажей основного клана, Академии, отрядов
и орденов по отдельности.<br>Чтобы принять персонажа в клан, необходимо
нажать кнопку «Invite», а затем выбрать, куда именно принять персонажа
– в основной клан, в Академию или в один из отрядов или орденов.<br>При нажатии на кнопку «Edit Emblem» откроется окно управления символом и эмблемой клана.<br>Появилось
дополнительное окно, в котором можно посмотреть список воюющих с кланом
противников, а также другую информацию, касающихся военных действий.
Окно можно открыть нажав на кнопку «War Info» <br><br><b>Прочие изменения</b><br>Максимальное количество кланов в альянсе – 3.<br>Штраф за выход/исключение из клана уменьшен с 5 дней до 1 дня.<br>Теперь у NPC Clan Hall Managers можно посмотреть дату и сумму очередного платежа ренты за владение клан холлом.</p><br><br>
