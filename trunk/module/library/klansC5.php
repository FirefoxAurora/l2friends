<p><b>Отряды Стражей и ордены Рыцарей</b><br>Лидер клана 6 уровня и
выше может создавать отряды Стражей. При создании отряда стражей один
из членов клана назначается главой отряда. Можно создать 2 отряда
Стражей. Максимальное количество персонажей в одном отряде – 20.<br>Лидер
клана 7 уровня и выше может создавать ордены Рыцарей. При создании
отряда стражей один из членов клана назначается главой отряда. Можно
создать 2 ордена Рыцарей на каждый отряд Стражей. Таким образом, можно
создать 4 ордена Рыцарей. Максимальное количество персонажей в одном
ордене – 10.<br>Отряды Стражей и ордены Рыцарей не могут быть
распущены, пока не будет распущен клан. Если клан покидает персонаж,
возглавлявший отряд или орден, то отряд или орден не исчезает, а на
место бывшего главы отряда или ордена можно назначить нового персонажа.
Назначить нового главу может только лидер клана, через NPC, отвечающего
за управление кланом. <br><b><br>Организационная структура клана</b><br>На рисунке приведена схема развитого клана:<br><img alt="" src="module/library/IMG/clan-1.jpg"> </p>
<p>Права членов клана, отрядов и орденов:</p>
<ul type="square">
<li>Основной клан, отряды и ордены имеют общее название и одинаковый
символ клана, также члены отрядов и орденов могут использовать клановый
чат и чат альянса. </li><li>Члены отрядов и орденов, могут использовать клан холл, замок
и доску объявлений наравне с членами основного клана. Однако, лидер
клана может лишить такого права весь отряд/орден. </li></ul>
<p>Члены отрядов и орденов могут принимать участие в осадах и войнах с
другими кланами наравне с членами основного клана – к ним будут
применяться стандартные правила.<br>В зависимости от организационной
структуры клана и прав, которыми обладает персонаж, ему будут доступны
только определенные действия. Так, члены отряда, могут обладать
привилегиями только внутри своего отряда. Например, члены отряда
Стражей не могут взять персонажа в Основной клан, но могут взять в
нижележащие ордены Рыцарей или в Академию.<br><b>Очки репутации клана (Reputation Score) </b><br>Клан,
достигший 5 уровня (и выше), начинает получать очки репутации за свою
деятельность и действия отдельных членов клана. Посмотреть количество
очков репутации, набранных кланом, можно в Adventurer's Guide Book.<br>Очки репутации можно заработать в следующих случаях:</p>
<ul type="square">
<li>Персонаж, входящий в состав Академии, получает вторую профессию. 
</li><li>Клан захватывает замок. Кланы, принимавшие участие в осаде на
стороне атакующих, но непосредственно замок не захватившие, не получают
очки репутации. </li><li>Клан удерживает замок, или помог удержать замок, принимая участие на стороне защитников. 
</li><li>Клан захватывает клан холл (BS, DC, PH и т.п.). Если до этого
клан холлом владел другой клан, то захвативший клан получит бонус к
очкам репутации. </li><li>Член клана принимал участие в Фестивале Тьмы, и при этом его
группа заняла первое место. Очки при этом распределяются в соответствии
с уровнями и званиями персонажей-победителей, принимавших участие в
Фестивале Тьмы. </li><li>Каждый раз, когда член клана становится Героем. 
</li><li>При выполнении квеста на повышение уровня клана. 
</li><li>При убийстве персонажа из клана, с которым ведется
двусторонняя война, а также при убийстве персонажей противоположной
стороны во время осады. Однако, если очки репутации клана, персонажа
которого убили, 0 или менее, то ваш клан не получит очки репутации. </li></ul>
<p>Очки репутации можно потерять в следующих случаях:</p>
<ul type="square">
<li>Если замок или клан холл, которым владеет клан, захвачен другим
кланом. При этом очки репутации будут вычитаться, даже если они уже
меньше 0. </li><li>При повышении уровня клана (на 6, 7 и 8 уровень) 
</li><li>При получении кланом новых клановых умений 
</li><li>При покупке различных вещей за очки репутации 
</li><li>При смерти члена клана во время двусторонней войны с другим
кланом или во время осады. Однако, если очки репутации клана меньше 0,
то при смерти члена клана они вычитаться не будут. </li></ul>
<p>За очки репутации члены клана могут купить специальный набор брони
A-класса Apella Set трех видов. NPC, у которых можно приобрести наборы,
расположены в Adena и Runa.<br><b><br>Навыки &nbsp;клана</b><br>Все
члены клана могут получить доступ к клановым навыкам. Для этого лидер
клана должен у специального NPC выучить навык, расплатившись очками
репутации и специальными вещами, которые можно добыть только с
рейд-боссов. По мере повышения уровня клана, появляется большее
количество скилов, которые может выучить клан.</p><img alt="" src="module/library/IMG/clan-2.jpg"> <br>
<p>Как только лидер клана изучает навык, он становится доступен всем
членам клана, включая персонажей, входящих в Академию, отряды и ордены,
если те отвечают определенным для этого навыка условиям. <br>Если очки
репутации клана опускаются ниже 0, то члена клана не могут пользоваться
навыками до тех пор, пока очки репутации клана не поднимутся выше 0.
Также, если очки репутации клана опускаются ниже 0, клан не сможет
выучить навыки, даже те, для изучения которых не нужны очки репутации.</p><br>
