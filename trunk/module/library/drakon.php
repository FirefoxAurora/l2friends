                  <p align="center">Характеристики юного 
                    Сумеречного дракона. Каши мало ел! </font></p>
                  <p><br>
                    Итак, прежде всего вам надо понять, что маленький дракоша, 
                    вам качаться помочь не сможет, потому что он сам еще ребенок, 
                    и поэтому же за ним нужен уход как за ребенком. На текущий 
                    момент (chronicles 1) дракоша при смерти теряет до 70% экспы, 
                    хотя обещают смягчить условия в следующем апдейте (chronicles 
                    2). После того как дракончик умер, его нужно оживить в течении 
                    3 минут, иначе он исчезает совсем. Набирает экспу дракон очень 
                    и очень не быстро. <br>
                    Момент номер 2, желудок у дракошы не больше наперстка, но 
                    жрет он, собака, как лошадь. Суть - вам нужно достаточно много 
                    еды. Кормить драконов &quot;с рук&quot; не получится, сами 
                    они есть тоже не будут. Вам нужно положить еду в их инвентори 
                    и кормить оттуда. Уровень сытости у дракоши желательно не 
                    опускать ниже 60%, начинает &quot;слабеть&quot; медленнее 
                    и слабее бить и бегать, если уровень сытости ниже 30%, дракоша 
                    откажется возвращаться в дудку (правда есть некоторая фича, 
                    если вы выйдете из игры, то дракончик окажется у вас в инветори), 
                    если уровень сытости упал до 0% - дракончик исчезнет. </font></p>

                  <p>У всех драконов очень маленькая атака, 
                    если вы хотите качать их в одиночку, то вам просто необходимы 
                    &quot;зубы&quot; для дракоши, причем чем лучше.. тем оно всегда 
                    лучше. Более того, если вы не являетесь обладателем &quot;Hatchling 
                    of the stars&quot; то вам просто показана броня для ребенка, 
                    иначе делевелы будут грустной реальностью. Не давайте дракону 
                    танковать, моб должен висеть на вас, при этом следует понимать, 
                    что дракон получит столько экспы, сколько он накусает с моба. 
                    <br>
                    Если у вас есть баффы, они должны быть на драконе (при новом 
                    вызове дракон появится без бафов. даже если вы на него их 
                    накладывали непосредственно перед тем как убрать его). <br>
                    А теперь один интересный момент, драконы могут быть в группе 
                    со своими собратьями Происходит это тогда, когда их хозяева 
                    находятся в группе, причем распределение экспы идет по уровням 
                    хозяев. <br>
                    Момент нумер два. Драконы могут получать вашу групповую экспу. 
                    Как это происходит? Если вы находитесь в группе с кем-либо 
                    и при убивании моба вы не делаете по мобу ударов, а ваш дракон 
                    делает хотя бы один результативный удар, вы получите 0 экспы, 
                    а дракон получит ту групповую экспу, которая должна была идти 
                    вам. Все это очень облегчает прокачку при этом не требуя экипировки 
                    для дракона. Естественно вам придется пожертвовать своей экспой 
                    и найти человека, который захочет качать вашего дракона в 
                    группе (наиболее реально это для клериков). И не забывайте, 
                    что при такой прокачке вам интересно максимум экспы с моба, 
                    что делает их уровень достаточно большим, а такой моб может 
                    убить дракона с нескольких ударов! (начиная с chronicle2 этот 
                    способ больше не работает) <br>

                    И еще.. когда покупаете что-то у Pet Manager'ов, не забывайте 
                    о налогах! В другом городе все может быть куда дешевле </font></p>
                  <p>Когда дракончик достигает 55 уровня, 
                    он может превратится в Страйдера (для этого нужно было иметь 
                    клан-холл, во вторых хрониках достаточно пройти специальный 
                    квест) </font></p>
                  <p>Страйдеры. <br>
                    Страйдера можно использовать так же как и других зверей. <br>
                    На нем можно кататься! </font></p>
                  <p>Появятся команды /ride /dismount соответственно 
                    чтобы забраться или слезть со страйдера. <br>

                    Когда вы едете на страйдере, ваша атака и скорость такая как 
                    у страйдера, а остальные статсы остаются теми же что были 
                    у вас. <br>
                    Если страйдер ниже вас на 5 уровней, атака уменьшается на 
                    50% и на каждые 5% за каждый последующий уровень разницы. 
                    <br>
                    Если страйдер ниже вас на 10 уровней, то дополнительно уменьшается 
                    скорость на 50% <br>
                    Если сытость страйдера падает ниже 50%, то скорость уменьшается 
                    вдвое и /ride /dismount команды начинают работать с ограничениями. 
                    <br>
                    Чем выше уровень страйдера, тем выше у него скорость! </font></p>
                  <p>Ограничения при катании на страйдерах: 
                    <br>

                    Можно использовать обычные атаки, но нельзя использовать физические 
                    скиллы. Все пассивные скиллы продолжают работать как и обычно. 
                    <br>
                    Баффы, лечения и дебаффы кастятся не на страйдера, а на вас. 
                    Так же всякие поушены будут тоже действовать исключительно 
                    на вас. <br>
                    Реколицца(soe) и телепортироваться можно не слезая со зверька. 
                    <br>
                    Но социал акции (смех и проч.чушь), а также торговля и крафт 
                    невозможны. <br>
                    При /dismount страйдер возвращается к вам в инвентори. И тоже 
                    самое происходит при выходе из игры или дисконнекте. <br>
                    Вся экспа идет лично вам. <br>

                    Пока вы едете на страйдере, он хочет меньше есть </font></p>
                  <p>Если страйдер вдруг помер, все так 
                    же как и с другими зверями. <br>
                    Если у страйдера сытость на 0% слишком долго, он исчезает. 
                    <br>
                    А если вы едете на нем, то на 0% он вернется в инвентарь. 
                    </font><br>
                  </p>
