                  <p>Тело Звездного дракона (Dragon of Star) 
                    покрывает слой настолько крепкой чешуи, что не любой ее сможет 
                    пробить. К сожалению, этот вид драконов довольно туповат, 
                    медлителен и очень прожорлив, зато зубы у них острые. Дракон 
                    Ветра (Dragon of Wind) обладает менее прочной чешуей, но очень 
                    злым характером. Зубы у этих созданий под стать характеру. 
                    Прожорливы в меру. Чешуя Сумеречного дракона (Dragon of Twilight) 
                    очень хрупкая. Они гораздо умнее своих старших собратьев и 
                    требуют меньше всего пищи. К сожалению, острота зубов этого 
                    вида драконов оставляет желать лучшего. </font></p>
                  <p>Звездные драконы - это такие мини-танки. 
                    Обладают спецумением &quot;вызывать огонь на себя&quot;, то 
                    есть типичным для любого танка умением Hate. Большое количество 
                    HP и самый высокий P.Def среди остальных драконов делает его 
                    незаменимым помощником мага, да и в критических ситуациях 
                    поможет продержаться до следующего лечения. Расход еды прямо 
                    пропорционален толщине чешуи - с одной единицы пищи счетчик 
                    сытости увеличивается всего на 7%. </font></p>

