             В самом начала все замки заняты кланами не-игровок (NPC).
  Как только клан игроков (PC) побеждает клан не-игроков и занимает замок, последний становится собственностью клана игроков. Если клан игроков, владеющий замком, распускается, замок переходит в своё первоначальное состояние (принадлежит клану не-игроков). <br />
  <br />
  Лорд замка устанавливает время, когда замок может быть осажден. Эта процедура осуществляется через управляющего NPC. Будьте внимательны, устанавливая время осады, т.к. однажды будучи установлено, оно больше не может быть изменено. <br />
  <br />
  Лорд, занявший замок, должен установить время осады в течении 24 часов после занятия замка. Если он или она не установит время осады в указанный промежуток времени, это время автоматически назначается игрой и следующая осада произойдет через 2 недели от времени захвата замка - в тот же самый день недели. (все осады происходят по выходным в режиме реального времени) <br />
  <br />
  Замку нельзя нанести повреждения пока не придет время осады. Следующим временем осады может быть суббота или воскресенье через две недели после того, как замок был занят. Клан, занявший замок, может спокойно владеть им следующие 15 дней. <br />
