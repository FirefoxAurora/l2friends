<h2>Special Abilities</h2>Многие оружия Ц-грейда, и, с С4, все оружия
Б-грейда или выше, могут иметь special ability (SA) (можно перевести
как «специальная возможность»), предназначенные для них (в С3 Б-грейд
меч Sword of Valhalla оставался без SA).<br><br>Для добавления SA к
вашему оружию, если это не дуалы, вам необходим Soul Crystal нужного
уровня и цвета (в соответствии с желаемыми SA). Для оружий до Б-грейд,
вы можете спросить у Blacksmith в Адене и Гиране список возможных SA
(не держите в руках оружие, когда спрашиваете, или они не найдут его),
и какого цвета нужен кристалл для каждого из них. Для А-грейд и S-грейд
обязателен Blacksmith of Mammon. Также для этого необходимы сотни
gemstone. <br><br>Для дуалов SA добывается путем овер-энчанта оружия до +4, с 30% шансом сломать их.<br><br>Вы
сможете достать soul кристаллы любого из трех цветов, только попросив у
мастера в гильдии магов в Гиране Квест. Вы также получите список мобов,
на которых вы сможете прокачать свой soul кристалл.<br><br>Увеличение
уровня soul кристалла происходит во время уничтожения мобов из списка.
Когда у тех остается половина здоровья или ниже, вы используете soul
кристалл (правым кликом в инвентаре или, разместив его на панели
быстрого доступа). После смерти моба, будут следующие возможные
сообщения:<br>
<ul>
<li>The soul crystal "fails to absorb the soul". Это случается чаще всего и означает, что ничего не произошло. 
</li><li>The soul crystal shatters. Кристалл разбился – возьмите еще один. 
</li><li>The soul crystal levels. Кристалл повысил свой уровень. 
</li><li>The soul crystal complains that the mob isn't strong enough. Поищите мобов более высокого уровня. </li></ul>Soul
кристаллы низких уровней (где-то до 7) не очень трудно достать. Уровни
8 и 9 довольно выполнимы. Начиная с уровня 10, прокачка кристалла
становится головной болью; приготовьтесь расколоть множество soul
кристаллов, перед тем, как все пройдет успешно. Замечания. Все
указанное ниже основывается на информации по С3 и уже частично по С4.
Все упорядочено по уровню кристаллов, по P. Atk и M. Atk. Если уровни
кристаллов одинаковые, то сортировка идет по P. Atk, поэтому сначала
идет магическое оружие, а оружие с наивысшей P. Atk находится в конце
списка. <div style="color: red;">Внимание: Новая информация о С4, особенно какие SA нуждаются в кристаллах какого цвета, на данный момент пока не надежна. </div><br><b>Определения</b> 
<ul>
<li>Good magic (хорошая магия): подразумевает все заклинания, которые являются баффами или лечением. <lАi>Bad
(harmful) magic (плохая (или вредоносная) магия): включает в себя
спеллы, наносящие урон и дебаффы (Dryad Root, Curse и т.д.) </lАi></li></ul><br><b>Типы Оружия </b><br><br>Мистики получают общий навык для оружия, позволяющий им быть полностью свободными в своем выборе. 
<ul>
<li>Sword (меч) означает «Одноручный меч». Все танки, Гладиаторы,
Свордсингеры и Дестроеры получают скиллы для использования мечей.
Однако, дестроеры не могут станить мечами, поэтому они предпочитают
Blunt. </li><li>Twohander (двуручник) означает «Двуручный меч». Только
Дестроеры получают специальные скиллы для этого оружия, но все, кто
может использовать одноручные мечи, также получают бонусы за
использование двуручников. </li><li>Blunt (топоры, молоты, дубины) означает «Одноручный Blunt».
Все танки, Гладиаторы, Свордсингеры, Дестроеры и все Гномы получают
скиллы для использования Blunt. Варкраеры, Оверлорды, Дестроеры,
Гладиаторы и Гномы получают специальные скиллы, позволяющие им станить,
используя Blunt оружие. </li><li>Staff (посох) означает «Двуручный Blunt». И снова, только
дестроеры получают специальный бонус за двуручное оружие, и снова все,
кто может использовать одноручное оружие, также могут носить двуручное.
Однако, все посохи, за исключением S-грейд Dragon Hunter Axe, являются
магическим оружием с высоким M.Atk и низким P.Atk и магическими SA.
Соответственно никто, кроме Мистиков их не использует. </li><li>Dagger (кинжал) может использоваться даггерщиками – Трэжер Хантером, Плэйнс Волкером и Абис Волкером. 
</li><li>Bow (лук) может использоваться лучниками – Хавкаем, Сильвер
Рейнджером и Фантом Рейнджером. Однако, некоторые воины предпочитают
иметь при себе лук, для дальних атак. </li><li>Fist Weapon (кастеты) используется Турантом. 
</li><li>Polearm (копье) используется Варлордом, а также Дестроером и Гномами. Однако, воин может носить копье для АоЕ атак. 
</li><li>Dual Sword (дуалы) используется Гладиаторами и Блейдденсерами. </li></ul><span style="color: rgb(0, 0, 0);"><span style="color: rgb(50, 205, 50);">Список SA</span> </span>
