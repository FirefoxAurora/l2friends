Часть IV.
<br>
NPC:Caradine 
<br>
Rewards:Noblesse Status, Tiara of the Noblesse
<br>Предварительные условия: Субкласс 75 уровня; Выполненные квесты
Путь Дворянина, Обладатель Ценной Души I15049 I15050 I15051 I15052 – 1,
2 и 3
<br>

<br>Поговори с Caradine в Годдарде. Её начальница хочет поговорить с
тобой. Она перенесёт тебя к ней, когда будешь готов. Сразу иди туда!
<br>

<br>Тебя перенесут к Колизею (Coliseum) в аккурат рядом с Хозяйкой
Озера Lady of the Lake. Побеседуй с ней и ты поймёшь, что на самом деле
это Богиня Ева M15052! Она попросит твоей постоянной поддержки в этом
раздираемом конфликтами мире. Если ты поклянёшься ей в этом, станешь
Дворянином ([46]) и получишь предмет - Корону Дворянина Noblesse Tiara.
Поздравляю! Теперь ты Дворянин ([46]) (и это работает на всех классах и
субклассах).
