Часть I.
<br>
NPC: Talien Награда: Опыт, возможность стать Дворянином Условия:
уровень подкласса – 50 Нужные материалы : 1 Hellfire Oil, 5 Lunaragents
(из квеста Supplier of Reagants - смотри ниже)
<br>
Начните квест у Talien, он находится на восточной стороне лестницы, ведущей в Церковь
<br>
Aden.
<br>
Телепортируйтесь в Giran. Там говорите с Gabrielle, она на южной стороне городской площади.
<br>Идите в Dragon Valley ко входу и говорите с Gilmore. (Если это
неделя наград в Семи Печатях и ваша сторона выиграла, вы не сможете
туда телепортироваться).
<br>Теперь направляйтесь в Dion, оттуда в Beehive. Пройдите немного на
северо-восток от места телепорта. Там вы найдете пещеру с нужными
монстрами, которых вы должны убить. Если вы не можете найти пещеру,
откройте карту и следуйте за указателем. Если вы прям на указателе то
монстров вы не увидите, вы находитесь на холме выше пещеры и должны
идти на юг - вход там. Убивайте монстров похожих на медуз, пока не
получите квестовую вещь.
<br>Возвращайтесь в Aden и говорите с Talien. Телепортируйтесь к
Saints Necropolis, используйте Scroll of Escape чтобы попасть в
Heine(?). Идите к Magic Trader и говорите там с гномом Melody Maestro.
<br>Опять в Giran, опять в Dragon Valley. Направляйтесь на запад.
Убивайте Malruk Succubus и Malruk Succubus Tauren, пока не получите 10
когтей. Запаситесь терпением, дроп этих квестовых вещей очень низкий.
Возвращайтесь в Heine и говорите с гномом.
<br>Теперь опять в Aden. Говорите с Talien. Затем направляйтесь в
Rune. Вам нужно в Rune Castle Town Temple. Поищите в храме Virgil, один
из трёх NPC на балконе. Запомните их кстати, они все для квеста о
Дворянстве. Говорите с Virgil, а потом и с 2мя другими.
<br>
Телепортируйтесь к Rune Castle Town Guild. Вбегите внутрь и говоряте с NPC там. Возвращайтесь к Rune Castle Town Store.
<br>Оттуда направляйтесь к западному входу Swamp of Screams. Убивайте
Splinter Stakato Drones, у них сплошные резисты, они наносят
кровоточащие раны. И вообще там все мобы социальны и кругом агры. На
50ом уровне в одиночку даже и не суйтесь. Из них будут падать crimson
moss (не зависит нанесли вы последний удар или нет), дроп ужасно
маленький – наберитесь терпения. Когда наберёте 5 штук, возвращайтесь к
NPC в Rune Guild.
<br>Возвращайтесь в Храм. Говорите там с девочкой, а потом с Virgil.
Он пошлет вас в Goddard к Caradine. Она к северо-востоку от GK на
внешней стороне города.
<br>Говорите с Caradine, и она пошлет вас к кузнецу. Он скажет, что
ему нужны 1 Hellfire Oil, 5 Lunaragents (квест Supplier of Reagants в
Ivory Tower). Когда получите их, возвращайтесь к кузнецу, а потом к
Caradine – всё! Вы получите опыт и письмо Virgil. В письме говорится,
что Virgil нуждается в помощи немедленно!
<br>
Поздравляю, вы закончили 1ую часть квеста. 3 осталось. Поговорите с Virgil для продолжения.
<br>
+
<br>
Часть II.
<br>
NPC: Master Virgil
<br>
Награда: Опыт, возможность стать Дворянином
<br>
Условия: уровень подкласса – 60
<br>

<br>Помните, Virgil нуждался в вашей помощи ПРЯМО СЕЙЧАС!!! Так почему
же вы ещё не там? Или вы уже там? Значит начинайте квест. Внучке
Virgila приснился ужасный сон о Принцессе, она нуждалась в помощи. И вы
конечно разберётесь с этим!
<br>
Тут описание может быть неточным.
<br>Вроде как вам надо найти кучку ангелов (Angel) и отвратительно
выглядящего NPC. Ангелы - вокруг Swamp of Screams, следуйте за
указателем. Говорите с NPC, он скажет вам, что пробовал спасти
Принцессу и ему пришлось убить всех этих ангелов. Он не знает, куда она
пошла. Осмотрите тела ангелов, они исчезнут, но потом снова появятся.
Продолжайте обследовать (или убивать?), пока не найдёте single blonde
hair. Говорите с NPC, он скажет вам идти к Witch Calis около Ivory
Tower. За Calis должок. Она с помощью заклинания может выяснить, где
принцесса.
<br>Телепортируйтесь к Ivory Tower и говорите с Witch Calis, он на
востоке от башни. Calis скажет, что ей нужны кое-какие запасы для её
заклинаний. Бегите к Alchemist Matild, он к западу от башни. Заберите
запасы и отнесите их к Calis. Вот тут неточность: либо говорите с NPC,
либо сразу идите в Valley of Siants – сами пробуйте.
<br>Пройдите через Valley of the Saints, пока не найдёте пещеру (там
где указатель). Убивайте квестовых монстров (они не социальны) для
получения Orbs of Binding. Дроп 100 %, так что кто-то в вашей партии
получит Orb в любом случае. Теперь тот кто первый набрал
<br>
4 Orbs пусть говорит со всеми Печатями (Seals) и использует на них сферы. Когда все
<br>
печати будут сломаны, появится Единорог, который на самом деле и есть Принцесса.
<br>
Поздравляю, вы спасли её! Возвращайтесь е Virgil. 
<br>
Говорите с Virgil и со всеми NPC там рядом. Вы получите опыт и письмо от Caradine.
<br>
Всё. 2ая часть закончена. На 65 уровне можете взять 3ью часть квеста.
