Часть III.
<br>
NPC: Caradine
<br>
Награда: опыт, возможность стать дворянином
<br>
Условия: уровень подкласса – 65,выполненные квесты
<br>
Путь дворянина, Обладатель Драгоценной Души - 1
<br>
Путь дворянина, Обладатель Драгоценной Души - 2
<br>

<br>1.Говорите с Caradine в Goddard Castle Town. Вы узнаете что
Принцесса очень слаба, и ей необходимы особые вещи для того чтобы
высвободить Силы и избавиться от проклятья. И конечно же Вы и только Вы
сможете спасти принцессу найдя их.
<br>
2.Сначала она попросит вас добыть кое-какие предметы из монстров в Valley of the Siants:
<br>Pilgrim of Splendor (который на самом деле Shine, ошибка в
переводе) и Judge of Splendor (Shine). Телепортируйтесь в Valley of
Siants (от Seven Signs priest), ну или можете добежать. От входа бегите
в долину а потом направо(юго-восток). Идите наверх пока не пересечёте
мост. Сверните направо. Там вы должны найти Pilgrims of Shine. Это
групповые монстры, и все 3 взрываются. Убивайте их пока не получите
квестовую вещь.
<br>
3.Оббегите вокруг вершины пока не найдёте Judges. Эти тоже взрываются. Бейте их до тех пор пока нужная вещь не окажется у вас.
<br>4.Верните вещи Caradine, выяснится что нужна ещё 3ья вещь. Чтобы
получить её вам придётся убить рейд босса 70 уровня в Valley of Siants
по имени Barakiel (Splendor/Shine).
<br>Идите на северо-восток в Valley of Siants чтобы найти его.
Следуйте за жёлтыми «кнопками».Вы увидите что-то вроде прозрачной
стены. Пройдите сквозь неё и вы увидите босса. Валите гада. Если вы
достаточно сильны то сможете забить его и в одиночку. Если вы в группе
то квестовая вещь достанется тому, кто нанесёт последний удар.
<br>
5. Как только цель достигнута возвращайтесь к NPC. Вы спасли принцессу!!!
<br>
Вы получите опыт и письмо разрешающее взять 4ую часть задания.
