Квест на Антараса
<br>

<br>
После выполнения этого квеста вы получите Portal Stone 1 который
позволит 1 раз пройти к Антарасу. Можно выполнять только с 50 левела.
<br>

<br>
Прохождение:
<br>

<br>
1. Поговорите с Gabrielle в Гиране. Она скажет вам посетить 4 Seal Watcher’ов.
<br>

<br>2. Поговорите с Magister Kaiena на 4 этаже Ивори. Она скажет вам
убивать Marsh Stalker и Marsh Drake(Blazing Swamp) пока не получите 1
Stalker Horn и 1 Drake Talon. Вернитесь к Kaiena, она даст вам Mark of
Watcher.
<br>

<br>3. Поговорите с High Priest Orven в Адене. Идите на Forbidden
Gateaway и убивайте там Bloody Queen пока не появятся Sacrifice of
Sacrificed. Бейте их пока не получите Remains of Sacrificed. Вернитесь
к Orven – получите второй Mark of Watcher.
<br>

<br>4. Поговорите с Master Kendra в Адене. Получите задание убивать
Harit Lizardman Shaman и Harit Lizardman Matriarch(Anghell Waterfall).
Бейте пока не появятся Harit Lizardman Zealot за убийство которых
получите Totem of Land Dragon. Возвращайтесь к Kendra за третьим Mark
of Watcher.
<br>

<br>5. Поговорите с Prefect Chakiris в Hunter’s Village. Он отправит
вас к Giant’s Cave. Убивайте там Kranrot и Hamrut пока не получите 1
Hamrut Leg и 1 Kranrot Skin. Вернитесь к Chakiris за Mark of Watcher.
<br>

<br>
6. Вернитесь к Gabrielle. Она скажет вам посетить 2 других Seal Watcher’ов.
<br>

<br>7. Поговорите с Warehouse Chief Moke в Глудине. Он скажет вам убить
Abyssal Jewel. Он находится на побережье возле гор, отделяющих
территорию Глудина от территории темных эльфов. В какой то момент когда
вы будете бить Jewel появятся его охранники. Убейте 1 из них чтобы
получить Mara Fang. Через некоторое время они исчезнут. Когда у Jewela
остается мало HP он исчезает, а вы получаете Abyss Fragment. Отнесите
это Warehouse Chief Moke – он даст вам Mark of Watcher.
<br>

<br>8. Поговорите с Blacksmith Helton в Орене. Он отправит вас к Sea of
Spores убить еще 1 Abyssal Jewel. Все как в прошлый раз – много HP и
охранников. Соберите Musfel Fang и Abyss Fragment и поменяйте их Helton
на последний Mark of Watcher.
<br>

<br>
9. Вернитесь в Гиран и поговорите с Gabrielle.
<br>

<br>10. Идите ко входу в Dragon Valley и поговорите с Eternal Watchman
of Antharas Gilmore. Он скажет что есть еще 1 Abyssal Jewel. Идите в
Dragon Valley и убивайте Cave Keeper и Cave Maiden пока не появится
Abyssal Jewel. У него всего 4 охранника, но после исчезновения
охранников исчезает и сам Jewel. Если вы не успели его убить то придеся
все делать заново, так что надо взять с собой побольше друзей. Когда
убьете Jewel получите Abyss Fragment. Несите его Eternal Watchman
Theodric у входа в Antharas Lair – он даст вам Portal Stone 1.
<br>

<br>
Теперь вы можете 1 раз пройти к Антарасу, активировав Heart of Wardening глубоко в Antharas Lair.