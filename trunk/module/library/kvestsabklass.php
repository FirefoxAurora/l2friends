Квест на субклассы (Subclasses Quest)
<br>
Эликсир Мимира (Путь к субклассу)
<br>

<br>
Прежде, чем Вы начнете квест, необхобимо следующее:
<br>
1) У вас должен быть 75 уровень.
<br>
2) У вас, должен быть закончен квест Fate's Whisper (A grade weapon)
<br>

<br>
Этот квест включает в себя выполнение двух различных квестов. Mimir's
Elixir как основной квест и Supplier of Reageants как дополнительный.
<br>

<br>1. Поговорить с Magister Ladd на 4-ом этаже Ivory Tower , Ladd
скажет Вам принести ему Pure Silver, которое может быть получено через
другие квесты - Supplier of Reageants.
<br>

<br>
2. Поговорить с Supplier Wesley в basment Ivory Tower.
<br>Он даст Вам квест Supplier of Reageants , который необходим, чтобы
получить квестовые вещи. Как только Вы получите эти квесты, Вы можете
говорить с Cauldron , расположенный в основании и видеть различные
вещи, которые Вы можете сделать, объединяя изделия квестов.
<br>
Единственные формулы, которые Вы должны знать от этого списка:
<br>
10 Moonstone shards + 1 Volcano Ash = 1 Moondust
<br>
10 Moondust + 1 Quick Silver = 1 Lunagent
<br>
1 Lunagent + 1 Quick Silver = 1 Pure Silver
<br>

<br>
3. Охотьтесь на Halate's Maids и Hames Orc Shaman.
<br>

<br>
Соберите:
<br>
100 Черепков Moonstone Shards от Halate's Maids на 3-ем этаже Тои
<br>
10 Volcano ashes от Hames Orc Shaman в Blazing Swamps
<br>
2 Quick Silver. Quick Silver выбивается из Halate's Maids и Hames Orc Shamans
<br>

<br>
4. Вернуть собранные изделия в Ivory Tower.
<br>
Как только Вы собрали все изделия, возвратитесь к Cauldron в IT, и объедините их согласно формулам, чтобы получить
<br>Pure Silver. Вы можете играть на деньги немного на меньших
реактивах и выбирать высокую температуру, чтобы получить больше чем
один.
<br>

<br>
Выберите 1 градус, чтобы обезопаситься от ненужного риска.
<br>
Однако, Вы должны выбрать 1 градус, делая Pure Silver. Это определено в квесте.
<br>

<br>
5. Принести Pure Silver к Ladd на 4-ом этаже IT (Ivory Tower).
<br>
Он скажет Вам, что он нуждается в True Gold.
<br>

<br>
6.Разговариваем с Magister Joann на 3 этаже Ivory Tower.
<br>
Она попросит, чтобы Вы пошли в Field of Silence , и охотились на Chimera Piece?. Пока не получите камень Stone of Wisdom
<br>

<br>

<br>7.Охота на Chimera Piece происходит под Giant's Cave в окрестности
Field of Silence моб стайный 72 lvl по квесту сказано, что даже класс
лекарей способен его убить, … Но так это или нет решать вам.. Как
выбьете Stone of Wisdom возвращайтесь в Ivory Tower на 3 этаж к
Magister Joann
<br>

<br>

<br>
8. Поговорите с Magister Joann и отдайте ей камень Stone of Wisdom взамен получите три куска золота.
<br>

<br>

<br>
9.Покажите три куска золота Magister Ladd на 4 этаже Ivory Tower…
<br>

<br>

<br>
10.Начните Охоту на Bloody Guardians.
<br>

<br>Bloody Guardians обитают в DVC убивайте их пока не выбьете Blood
Fire. Для получения Blood Fire необязательно наносить последний удар по
Bloody Guardians именно вами это может сделать любой член пати…
<br>

<br>
11. Возвращайтесь в Ivory Tower
<br>Идем к котлу и используем все собранные реагенты Pure Silver, True
Gold, Blood Fire… Для варева используем 3 степень... Получаем Mimir's
Elixir.. Ходят слухи что при 3 степени варки реагенты могу исчезнуть
бесследно но таких случаев небело зафиксировано…
<br>

<br>
12. Возвращаемся с Mimir's Elixir к Ladd…
<br>

<br>Как только вы поговорите с Magister Ladd , эликсир должен исчезнуть
из инвентаря, и квест будет закончен. Также в качестве награды вы
получите A grade weapon enchant.. Да и не пытайтесь выпить Mimir's
Elixir иначе будете все делать заново.. Хотя особо одареные могут
выпить эликсир, а вот, что он даст, увы, неизвестно…
<br>

<br>

<br>Вы теперь заработали право получит субкласс..Для этого
отправляйтесь к blacksmith, Grand Master, Grand magister и т д.. В
зависимости от класса, который Вы хотите выучить. Например, если Вы
хотите sub Гладиатору, Вы говорите с Grand Master (там, где получают
Skilli Himan).
<br>

<br>
Ограничения:
<br>

<br>
Вы не можете выбирать аналогичные профессии как субкласс. Ниже приводятся аналогичные профессии:
<br>

<br>
* Treasure Hunter, Plainswalker и Abyss Walker
<br>
* Hawkeye, Silver Ranger и Phantom Ranger
<br>
* Warlocks, Elemental Summoner и Phantom Summoner
<br>
* Dark Avenger, Paladin, Temple Knight и Shillien Knight
<br>
* Sorcerer, Spellsinger и Spellhowler * Elder и Shillien Elder
<br>
* Swordsinger и Bladedancer
<br>

<br>
Другие замечания:
<br>
1. Вы НЕ можете изменить субкласс который выбрали
<br>
2. Когда ваш субкласс достигнет 75 лвла вы можете выполнить квест Mimir's Elixir снова и качать другой субкласс.
<br>
3. Вы можете выбрать до трёх субклассов
<br>
4. Опасайтесь изменения с кол-вом веса когда вы переключаетесь на субкласс.
<br>
5. Все баффы исчезают когда вы переключаетесь на субкласс.
<br>
6. Квест на пожирателя грехов (летающий дракон) исчезает когда вы переключаете классы.