<u><b>Artisan</b></u> - Квест начинается с 19 уровня <br>
<br>
01. Говорим с Blacksmith Silvery(Dwarven Village). Для того чтобы стать
Artisan'ом нужно получить разрешение у трёх Кузнецов которые были
Артизанами очень долго. Она даст Silvery's Ring.<br>
02. Убиваем Boogle Ratmen и Boogle Ratman Leaders(местонахождение -
довольно глубоко в подземельях Mithril Mines) - собираем 10 Boogle
Ratman Teeth и 2 Boogle Ratman Leader's Big Teeth.<br>
03. Отдаём зубы Blacksmith Silver(Dwarven Village). Она даст нам 1st Pass Certificate.<br>
04. Говорим с Blacksmith Kluto(Gludin) чтобы получить Kluto's Letter.<br>
05. Говорим с Blacksmith Pinter(Gludio). Он возмёт письмо и даст нам Footprint of Thief.<br>
06. Бьем Vuku Orc Fighter'ов(43.5-50 рядом с Gludio Castle) пока не выпадет Stole Secret Box1.<br>
07. Относим Box и Footprint BLacksmith Pinter(Gludio) и он даст нам Secret Box2 и 2nd pass Certeficate.<br>
08. Возвращаемся в Gludin и отдаём Box и Certificates Blacksmith'у
Kluto. Не надо говорить ему как мы это получили, а-то у Pinter'а будут
проблемы. Он даст нам Final Pass Certeficate.<br>
09. Говорим с Head Blacksmith Tapoy(Gludin) и становимся Artisan'ом.</div>
