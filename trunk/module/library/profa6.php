<div id="post_message_79"><u><b>Scavenger</b></u> - Квест начинается с 19 уровня<br>
<br>
01. Говорим с Collector Pippi(Dwarven Village). Она даст нам Pippi's Letter of Introduction.<br>
02. Говорим с Trader Mion(Dwarven Village). Она даст нам пачку
повторяющихся курьерских маршрутов по городу. Выполняем до тех пор пока
не даст Mion's Letter.<br>
03. Ищем Master Toma. Он в одной из трёх локаций, он портуется из одной в другую раз в 30 минут.<br>
а. Небольшой островок на севере - можно портануться туда через Gatekeeper Wirphy(Dwarven Village).<br>
б. Северный выход из Mithril Mines <br>
в. Cеверный выход из Abandoned Coal Mines<br>
04. Убиваем Hunter Bears рядом с северо-восточным teleport point'ом.
Через каждые 20-30 убийств будет появляться Honey Bear(Quest monster).
Каждый Honey Bear роняет по Honey Jar'у. Обязательно используйте Spoil.
Собираем с них 5 Honey Jaro'ов.<br>
05. Ещё раз находим Master Toma чтобы получить Bear Picture.<br>
06. Убиваем Hunter и Plunder Tarantul'ов,пока не соберём 20 Beads. Они находятся к востоку от медведей.<br>
07. Опять ищем master Toma и даём ему Beads. Он даст Bead Parcel и отправит в Gludin.<br>
08. Говорим с Warehouse Keeper'ом Raut'ом(Gludin). Он обменяет Parcel на Rout's Teleport Scroll.<br>
09. Относим скролл к Torai(51.6-51.8), он стоит рядом с западной границей Dragon Valley. Он даст Succubus undies.<br>
10. Вернёмся к Raut'у в Gludin. Он возмёт Undies и даст Ring of Raven.<br>
11. Идём к Warehouse Chief Moke(Gludin) и становимся Scavengerom.<br>
