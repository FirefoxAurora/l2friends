<u><b>Human Knight </b></u>- Квест начинается с 19 уровня <br>
<br>
01. Говорим с Sir Klaus'ом Vasper'ом(Gludin). Ему нужно 3 Coin of Lords.<br>
02. Говорим с High Priestess Levian(Gludin). Ей нужно 20 Langk Lizardmen Totems.<br>
03. Говорим с Captain Bezique(Gludin). Ему нужно 20 Poison Spider Legs.<br>
04. Поговорив с Levian и Bezique можно делать их квесты одновременно,
потому что и Лизардмены и Пауки живут вместе к югу от Глудина (к западу
от дороги 40-55).<br>
05. Получив ноги и тотемы несём их Bezique и Levian и получаем 2 Coin of Lords.<br>
06. Говорим с Captain Bathis(Gludio). Ему нужно 10 Bugbear Necklaces,
которые есть у Bugbear Raider'ов(46-51 к северо-востоку от Глудио).<br>
07. Относим 10 Bugbear Necklaces к Bathis'у и получаем третью Coin of Lords.<br>
08. Говорим с Vasper'ом. Он даст Sword of Ritual.<br>
09. Идём к Grand Master'у Ramos'у(Gludin). И становимся Human Knight.<br>
