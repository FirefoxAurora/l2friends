<u><b>Human Rogue</b></u> - Квест начинается с 19 уровня <br>
<br>
01. Говорим с Captain Bezique(Gludin). Oн даст Bezique's Letter.<br>
02. Несём письмо к Neti(Gludin). Она даст нам Neti's Dagger and Bow. На протяжении всего квеста можно использовать <u>только</u> это оружие.<br>
03. Идём в Ruins od Agony. Вырезаем Spartoi (только кортиком или
луком). Собираем 10 Spartoi's Bones. Так же можно охотиться на
скелетов, но они редко оставляют кости.<br>
04. Относим кости к Neti(Gludin) и получаем в замен Horeshoe of Light.<br>
05. Относим Horeshoe к Bezique(Gludin) и меняемся с ним на Wanted Bill.<br>
06. Охотимся на Cat's Eye Bandits(42-53) (опять же с Луком или
Кортиком), пока не найдём 4 веши - Stolen necklace, Stolen Tomes,
Stolen Jewelry и Stolen Ring.<br>
07. Говорим с Bezique(Gludin). Он заберёт все квестовые вещи и даст Bezique's Recommendation<br>
08. Идём к Grand Master'у Ramos'у(Gludin) и становимся Human Rogue.<br>
