06. Убиваем Poison Spiders и Arachnid Trachers(46-53) к юго-западу от
Gludio пока не соберем 20 паучих конечностей. Убивать пауков нужно <u>только</u> Rusted Bronze Sword'ом.<br>
07. Относим Aynus'у. Он даст Medallion of Warrior.<br>
08. Идём к Grand Master'у Ramos'у(Gludin). И становимся Human Warrior.<br>
