				<div class=np03><span style="COLOR: lightgreen">Созерцание Великой Олимпиады</span></div>
				Зрители могут следить за поединками на Олимпиаде через Grand Olympiad Manager'а, находящегося в каждой деревне.
				Они видят поединок и имена участников, их HP, CP и наложенные на них заклинания.<p>
				Находясь в Колизее, зрители могут следить за происходящим в других Колизеях, не покидая своего.<p>
