<u><b>Dark Elf Palus Knight</b></u> - Квест начинается с 19 уровня<br>
<br>
1. Поговорите с Master Virgil, он находится в гильдии Dark Elf в
Gludio, он даст вам Pallus Talisman. Ему срочно понадобилось 13 черепов
Lycanthrope.<br>
2. Убиваем Lycanthrope пока не соберем 13 Lycanthrope Skull.(41.5-48.5 рядом с Алтарем Эльфов) <br>
3. Вернитесь в Глудио, поговорите с Master Virgil, получите от него Virgil"s Letter.<br>
4. Отнесите письмо в Gludin Abyssal Celebrant Kalinta и получите Morte Talisman.<br>
5. Теперь идите и убивайте Poison Spiders(водятся рядом с Gludin 40-55) пока не получите Predator"s Carapace. <br>
6. Теперь надо убить 5 Arachnid Tracker, из каждого вы получите по одному Trimden Silk.(41.5-48.5 рядом с Алтарем Эльфов).<br>
7. Возвращайтесь в Gludin к Kalinta, получите Coffin Eternal Rest.<br>
8. Теперь обратно в Gludio к Master Virgil, получите от него Gaze of Abyss.<br>
9. После получения 20 уровня поговорите с Grandmaster Tobias и вы Palus Knight.<br>
