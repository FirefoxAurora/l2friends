                  <p>Это довольно сложное задание. Что нужно, 
                    чтобы его успешно выполнить. Нужны как минимум один лекарь 
                    любого уровня, группа воинов или высокоуровневый маг с массовым 
                    усыплением. </font></p>
                  <p>3. Вам необходимо найти последовательно 
                    4 дерева соответствующих квестовым вещам названий. </font></p>

                  <p>4. Найдя одно из деревьев, Вы вызываете 
                    дракона и даете ему приказ атаковать дерево. </font></p>
                  <p>Находящиеся поблизости Tree Guardian 
                    начинут атаковать дракона. <br>
                    1. Тут и нужны воины или маги, чтобы быстро убивать/усыплять 
                    духов, дабы они не трогали дракона. Убивать духов менее эффективно, 
                    так как они появляются заново через 2-5 секунд, а время действия 
                    слипа – 30 секунд. <br>
                    Духов можно и нужно держать подальше от дракона любым способом. 
                    <br>
                    2. Вы не должны атаковать дерево, дерево должен атаковать 
                    только дракон. Более того. <br>
                    ДЕРЕВО НЕЛЬЗЯ УБИВАТЬ! Ни при каких обстоятельствах. Лекарь 
                    должен работать на 3 фронта – лечить дракона, дерево и тех, 
                    кто разбирается с охраной дерева. <br>

                    Если Вы случайно ударите дерево, то дерево наложит на Вас 
                    заклинание Poison. Мощность: 10 (невозможно снять). Если Вы 
                    или Ваш дракончик все же убьете дерево, то дерево появится 
                    снова через 5-15 минут, а вас самих будет ждать неприятный 
                    сюрприз. <br>
                    3. Желательно не накладывать на дракона бафы, усиливающие 
                    атаку, чтобы он случайно не убил дерево, лучше дайте ему ускорение 
                    атаки, защиту и регенерацию здоровья. <br>
                    5 Приблизительно через 100 с небольшим ударов дракончика по 
                    дереву, дерево зашевелится и запросит пощады и помощи. При 
                    этом у Вас из инвентаря пропадет один из листов. <br>
                    6 Для остальных деревьев тактика аналогичная. Искать деревья 
                    необходимо вдоль гор к югу от Hunter Village.Используем SoE. 
                    Телепортируемся в Ivory Tower. Бежим к северному входу в Enchanted 
                    Valley. Входим в долину и сразу же сворачиваем на восток. 
                    <br>
                    7 Ищем Fairy MyuMyu. Вызываем дракончика. Разговариваем с 
                    феей. Отзываем дракончика. Разговариваем с Fairy MyuMyu. Дракончик 
                    исчезнет, а Вы получите Dragon Bugle того же типа, что был 
                    Ваш дракончик. <br>
                    Теперь Вы – счастливый обладатель Страйдера. <br>

                    Приятной поездки! <br>
