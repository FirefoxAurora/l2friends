                    питомца впереди? <br>
                    А впереди нелегкое испытание для дракончика. Чтобы превратить 
                    дракончика в здового дракона (Strider), придется попотеть 
                    не только Вам, но и Вашему дракончику. Причем дракончику придется 
                    куда хуже, чем Вам. </font></p>
