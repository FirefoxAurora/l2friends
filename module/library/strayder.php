                    <strong>ПРИМЕЧАНИЯ К ПРОХОЖДЕНИЮ </strong></font></p>
                  <p>Ищем Cмотрим, где искать (названия 
                    мобов и NPC - ссылки на карту), бежим в нужное место.Разговариваем 
                    Выбираем опцию QUEST, пробегаем все диалоги, если в диалогах 
                    встречаются строчки на выбор, то смотрим, какую строчку выбирать. 
                    Если в описании такой строчки не указано, значит: а) это неважно, 
                    б) описание неполное. </font></p>
                  <p>Примечание Чтобы получить профессию, 
                    в начале разговора с NPC необходимо выбрать не QUEST, а INFORMATION 
                    ON CHANGE OCCUPATION. <br>

                    Во втором меню - выбрать желаемую профессию. В третьем меню 
                    - выбрать CHANGE OCCUPATION.Получаем Указана вещь, которая 
                    будет выдана, но не указано, какие вещи заберутОхотимся на 
                    Х, пока не соберем N Y Вероятность получить квестовую вещь 
                    с некоторых мобов иногда довольно низкая, но не более 10 мобов 
                    на 1 квестовую вещь (в отдельных случаях - до 30-50), если 
                    больше - придется начинать задание сначала.Передвижение Не 
                    обязательно пользоваться телепортами, можно сэкономить, прогулявшись. 
                    <br>
                    Используем SoE - Scroll of Escape, свиток возврата. Важно! 
                    У Вас должен быть установлен патч SoE Map (для C1, в С2 можно 
                    пользоваться командой /loc), чтобы знать, куда Вы попадете 
                    при использовании SoE. <br>
                    В любом случае, рекомендую смотреть, где искать нужных NPC, 
                    чтобы не возникло ситуации, когда Вы из-за ошибки будете искать 
                    их в совсем других городах. </font></p>
                  <p>Управление страдером в режиме помошника 
                    не отличается от управления другими питомцами. </font></p>
                  <p>Игрок призвавший страйдера может сесть 
                    на него используя кнопку &quot;Mount/Dismount&quot; или команду 
                    /mount </font></p>

                  <p>Когда вы садитесь на страйдера его 
                    параметры изменяются. Физический урон и скорость передвижения 
                    отражает параметры страйдера, в то время как остальные способности 
                    отражают параметры игрока. Однако, если уровень игрока намного 
                    ниже уровня страйдера на физический урон и скорость передвижения 
                    будут распространяться штрафы. Например, если уровень игрока 
                    ниже уровня страйдера на 5, то физический урон страйдера будет 
                    уменьшен на 50%, если разница в уровне еще больше, то на каждый 
                    дополнительный уровень будет налагаться штраф -5% к физическому 
                    урону. Если же разница в уровне составляет 10, то скорость 
                    перемещение страйдера уменьшается в двое. </font></p>
                  <p>Если уровень сытости страйдера опускается 
                    до 50% и ниже, то скорость передвижения страйдера уменьшается, 
                    так же вы не сможете слезть со страйдера или отозвать его 
                    в инвентарь. Еду для страйдера вы можете преобрести в любом 
                    городе у Pet Manager'а. </font></p>
                  <p>С повышением уровня страйдера увеличивается 
                    скорость его передвижения, при понижении уровня - уменьшается. 
                    </font></p>
                  <p>Если страйдера убивают, то он, так 
                    же как и другие питомцы, некоторое время будет отображаться 
                    в виде трупа. Если вы не успеете воскресить его за это время, 
                    то он пропадет навсегда. </font></p>
                  <p>Если уровень сытости страйдера опускается 
                    до 0% и в течение некоторого времени вы не прелпринимаете 
                    попыток накормить его, то он исчезает. Если в этот момент 
                    вы сидели на нем, то страйдер отзывается в инвентарь, и при 
                    следующем его вызове уровень его сытости будет равен 0%. </font></p>
