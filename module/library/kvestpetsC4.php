Квест на Kookaburra
<br>

<br>

<br>

<br>
Уровень : 24
<br>
Класс: все
<br>
Начало: Gludio Castle Town - Pet Manager Lundy
<br>
Повторное прохождение: Pet Exchange
<br>
Билет: Kookaburra
<br>

<br>

<br>

<br>
1. Поговорите с Pet Manager Lundy. Он попросит вас принести ему Work Hammer (~450 000 Adena в магазине).
<br>

<br>
2. Он попросит вас убить Maille Lizardman Scouts Lizardman, Maille
Lizardman Guards или Maille, которые находятся в Gludio и Maille
<br>

<br>
Lizardman Barracks.
<br>

<br>
Собрать 30 фрагмент вопроса. Он попросит вас навестить High Prefect Dirkus (entrenneur Orc в Gludio)
<br>

<br>
3. Поговорите с Dirkus, Pet Manager Lundy. Lundy вам даст тогда Pet Exchange Ticket: Kookaburra.
<br>

<br>
4. Вновь поговорите с ним и обменяйте билет чтобы получить Kookaburra.
