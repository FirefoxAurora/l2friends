Квест на Cougar
<br>

<br>

<br>
Уровень: 26
<br>
Класс: все
<br>
Начало: Giran Castle Town - Pet Manager Cooper
<br>
Повторное прохождение: Pet Exchange
<br>
Билет: Cougar
<br>

<br>

<br>

<br>
1. Поговорите с Pet Manager Cooper, который находится в Giran (в Luxury Shop).
<br>

<br>
Он попросит вас принести ему Crafted Dagger (~450 000 Adena в магазине).
<br>

<br>
2. Он попросит вас собрать 30 Map Fragments убивая Specters и Sorrow Maidens, которых вы сможете найти в Execution Grounds.
<br>

<br>
3. Он соберет map и вас спросит о ammener в Galladuci, который оказывается в Luxury Shop.
<br>

<br>
4. Galladuci вас remercira. Reparter видеть Pet Manager Cooper, который вам даст тогда Pet Exchange Ticket: Cougar.
<br>

<br>
5. Вновь поговорите с ним и обменяйте билет чтобы получить Cougar.
<br>

