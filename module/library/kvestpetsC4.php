Квест на Buffalo
<br>

<br>

<br>
Уровень: 25
<br>
Класс: все
<br>
Начало: Dion Castle Town - Pet Manager Waters
<br>
Повторное прохождение: Pet Exchange
<br>
Билет: Buffalo
<br>

<br>

<br>

<br>
1. Поговорите с Pet Manager Waters, который находится в Dion. Он попросит вас принести ему Trydent (~450 000 Adena в магазине).
<br>

<br>
2. Он попросит вас убить 30 Monster Eye Gazers и/или Monster Eye Destroyers (30 в общей сложности, не 30 каждый =).
<br>

<br>
Вы найдете их у Floran Village.
<br>

<br>
3. Он попросит вас подойти к Sophya, которая находится также в Dion.
<br>

<br>
4. Поговорите с Sophya и с reparter видеть Pet Manager Waters.
<br>

<br>
Он даст вам Pet Exchange Ticket: Buffalo.
<br>

<br>
5. Вновь поговорите с ним и обменяйте билет чтобы получить Buffalo.
<br>

