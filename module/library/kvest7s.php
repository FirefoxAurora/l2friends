4) Вход в подземелья 
<br>
В течение первой недели времени (далее так называемого периода
соревнования), когда собираются печати (хм, а вроде бы руны и мантры
собираться должны были, переводчик замордовался, наверное – SSS), любой
персонаж, вступивший в сообщества может войти. <br>

<br>

<br>
После того, как заканчивается период состязания, в течение следующей
недели (далее так называемого времени действия печати), в течение
которой проявляется действие печати, только сторона, обладающая этой
печатью может войти. Во время недели действия печати те, кто были
побеждены, м даже победители состязания, которые завладели печатью, но
не смогли набрать необходимый % не могут войти в подземелье. <br>

