5) Монстры внутри подземелий 
<br>
- Членам каждого сообщества нужно охотиться на монстров в подземельях,
чтобы добывать руны и мантры. Из нефилимов выпадают руны, необходимые
членам сумрака, и из лилимов выпадают мантры, необходимые членам
рассвета. <br>
- Во время состязаний, спавнятся обычные монстры, и лилимы, и нефилимы,
но в течение времени действия печати, печати должны быть заполучены, и
спавниться будут монстры, из которых выпадает необходимая стороне
победителя вещь (мантры будут выпадать для членов рассвета, и руны
будут выпадать для членов сумрака). <br>

