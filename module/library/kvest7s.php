<br>
1-5. Действия печатей 
<br>

<br>
Когда любая из печатей находится во владении, происходят следующие изменения. 
<br>

<br>
1) Seal of Avarice (Печать Алчности) 
<br>

<br>
* Телепортирующий NPC на входах в некрополи будет телепортировать в
подземелья только членов победившей стороны, а также предоставит
разнообразные баффы за плату в древних адена. Тем, кто не обладает
печатью, войти нельзя. <br>
* Внутри некрополя создаются монстры, на которых могут охотиться члены победившей стороны. 
<br>
* В некрополе Апостола (Apostle's Necropolis) можно встретить Анакима и
Лилит. Анакиму и Лилит нужны "blessed soul collection stone and [lead?]
pipe" (Тут я бессилен: какой-то благословлённый камень для сбора душ,
хотя это не Морровинд, и труба. В квадратных скобках, видимо
переводчиковское подозрение, что труба свинцовая – SSS), чтобы придать
A-grade-оружию особые свойства. <br>
* Внутри некрополей появится "Mamon's Trader" (Торговец Мамона).
Используя древние адена для оплаты, можно купить разнообразные вещи,
которые не найти в обычных магазинах. "Mamon's Trader" не привязан к
какому-то конкретному месту, а перемещается между подземельями в любой
момент. <br>

<br>
2) Seal of Revelations (Печать Откровений) 
<br>

<br>
* Телепортирующий NPC на входах в подземелья будет телепортировать в
подземелье только членов победившей стороны. Тем, кто не обладает
печатью, войти нельзя. <br>
* Победители могут заплатить служителю в городе древними аденами для
телепортации в определённые охотничьи угодья ("места кача" тоже
некрасиво smile.gif – SSS). <br>
* Глашатай краха (The crier of ruin) скитается по каждому городу (за
исключением городов для начинающих) и будет случайным образом кастовать
вредоносную магию на членов стороны, которая проиграла. <br>
* Просветитель откровений (The illuminator of revelations) блуждает по
каждому городу (за исключением городов для начинающих) и будет
случайным образом кастовать полезную магию на членов стороны, которая
