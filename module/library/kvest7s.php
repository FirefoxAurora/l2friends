1-7. Прочая информация 
<br>

<br>
1) Руны и мантры 
<br>
Руны и мантры все можно обменивать, и, чтобы их достать, необходимо
идти в подземелья и некрополи, дабы убитвать лилимов и нефилимов. Руны
получаются теми, кто на стороне сумрака, убийством нефилимов. Мантры
получаются теми, кто на стороне рассвета, убийством лилимов. <br>
Ценность руны растёт в следующем ряду: Вода &lt; Ветер &lt; Огонь 
<br>

<br>
2) Древние адена 
<br>
Древние адена, которые могут быть получены посредством рун и мантр,
могут быть отданы служителям рассвета и сумрака, кузнецу Мамона
(Mamon's Blacksmith), и торговцу Мамона (Mamon's Trader) для покупки на
них вещей или могут применятся при использовании различных скиллов. <br>

<br>
3) Chronicle of the Seven Seals (Летопись "Семи печатей") 
<br>
С этим предметом можно окинуть взглядом, увидеть общую сводку "Семи
печатей" и основного ивента ( – SSS), состояние печатей и т.д. Любой
может воспользоваться служителями рассвета и сумрака. <br>

<br>
4) Monarch's Seal of Approval (Знак одобрения монарха) 
<br>
С этим предметом персонажи, которые закончили вторую смену профессии,
но не являются частью замкодержащего клана или альянса могут
присоединиться к стороне рассвета. <br>

<br>
5) Mamon's Dealers (Дельцы Мамона) 
<br>
Меняет мантры на руны или руны на мантры. Переводит адена в древние адена.