<u><b>Elven Knight</b></u> - Квест начинается с 19 уровня <br>
<br>
01. Говорим Master Sorius(Gludio) что хотим стать Elven Knight'ом. Он
отправляет в Ruins of Agony(42-51) чтобы собрать 20 Topaz Pieces с
Undead Priest.<br>
02. Несём топазы к Sorius. Он даст письмо и отправит забрать Elven Knigh Brooch у Kluto(Gludin).<br>
03. Идем к Blacksmith Kluto. Он попросит убить Ol mahum Novices(42-53)
в Abandoned Camp и принести ему 20 Emerald Piecec как доказательство.<br>
04. Собираем 20 Emerald Piecec и возвращаемся к Kluto(Gludin). Он
обменяет Emerald и Topaz Pieces на собранный Elven Knight Brooch.<br>
05. Возвращаемся к Master Sorius(Gludio). <br>
06. Далее подходим к Grand Master Rains(Gludio) чтобы стать Elven Knight'ом.<br>
