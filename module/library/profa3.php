<div id="post_message_65"><u><b>Elven Scout</b></u> - Квест начинается с 19 уровня <br>
<br>
01. Говорим с Master Reisa(Gludio). Она попросит найти потерянного друга Guard'а Moretti. Она даст Reisa's Letter.<br>
02. Говорим с Guard Moretti(Gludio). Он заберет письмо.<br>
03. Идём к Abandoned Camp и режем Ol Mahum Patrols(41-53) пока не соберём Prigun's Tear Letter 1,2,3 и 4.<br>
04. Несём письма к Moretti(Gludio). Он заберёт все письма и даст Moretti's Herb и Moretti's Letter.<br>
05. Говорим с Pippen(45-47.5)<br>
06. Убиваем Ol Mahum Sentry (рядом с Pippen45-47.5) которые охраняют Pippen пока не получим Rusted Key.<br>
07. Освобождаем Pippen'а. Он возмёт Herb и Letter и даст нам Pippen's (Prigun's) Letter.<br>
08. Несем письмо к Guard Moretti(Gludio). Он даст нам Honorary Guard Certificate.<br>
09. Говорим с Master Reisa(Gludio). Она заберёт Certificate и даст Reisa's Recommendation.<br>
10. Идём к Grand Master Rains'у (Gludio) чтобы стать Elven Scout'ом.<br>
