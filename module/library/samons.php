                  <p><font color="#0D0000">У каждого самона свое назначение. Один 
                    может лечить, другой специализируется на ближнем бою, третий 
                    - восстанавливает ману и так далее. <br>
                    Самых мощных существ могут вызывать только заклинатели существ 
                    - самонеры, знающие не только как вызвать, но и как контролировать 
                    самона. Каждый самон к сожалению, самоны привязаны к тому 
                    миру, из которого они были вызваны - в новом мире их удерживает 
                    только физическая оболочка, а она недолговечна. И по прошествии 
                    определенного времени физическая оболочка разрушается, и самоны 
                    возвращаются обратно в свой мир. <br>
                    Оболочку самона также могут разрушить и враги. Еще один тип 
                    самонов - кубики - доступен только определенным классам. </font></p>
                  <p><font color="#0D0000"><strong>Магический куб. </strong></font></p>
                  <table width="100%" cellspacing="1" cellpadding="1">

                    <tr> 
                      <td><div align="center"><img src="images/pets/image045.jpg" width="142" height="132"></div></td>
