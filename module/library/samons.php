                  <p><font color="#0D0000">Куб относится к созданиям, которые 
                    безропотно исполняют все задания своего хозяина. </font></p>
                  <p><font color="#0D0000">Куб могут вызывать Рыцари Храма и Рыцари 
                    Шиллен. Во время занятий охотой, куб может принимать участие 
                    в атаке, как непосредственно, так и косвенно. Для того, чтобы 
                    вызвать Куб необходимо определенное количество кристаллов 
                    и навык. кубы определенно добавят забавы в мире Lineage II. 
                    <br>
                    Типы кубов <br>

                    Штормовой куб (Storm Cubic): Атакует цель, которую укажет 
                    ему хозяин кинжалом воздуха. <br>
                    Вампирический куб (Vampiric Cubic): Обладает свойствами вампира, 
                    способного подпитывать физическую силу своего хозяина. <br>
                    Куб-полтергейст (Poltergeist Cubic): Насылает на своего врага 
                    проклятье, такое как Замедление (Slow), Проклятье (Curse), 
                    Слабость (Weakness) и т.п. <br>
                    Куб жизни (Life Cubic): Восстанавливает HP своего хозяина. 
                    <br>
                    Куб-змей (Viper Cubic): Отравляет своего врага, уменьшая его 
                    HP. <br>
                    Характеристики Куба <br>

                    В зависимости от типа навыка хозяина, Куб может иметь возможность 
                    множественного вызова. <br>
                    Нет специального окна для управления кубом. Куб действует 
                    в том же направлении, что и хозяин. <br>
                    Наряду с Кубом можно держать домашнее животное или вызванную 
                    сущность. <br>
                    Куб не может выбирать цель, при этом он сам не может быть 
                    атакован. Однако он может быть снят рассеивающим волшебством. 
                    </font></p>
                  <p><font color="#0D0000"><br>
                    Приручение по сути означает &quot;одомашивание или тренеровка&quot;. 
                    В Lineage II игрок может приручать уже существующих NPC-существ. 
                    В отличии от сотворенных или магических существ, которые могут 
                    быть ограничены по времени существования или устранены при 
                    помощи магии, прирученные создания не ограничены по времени 
                    существования этими факторами. </font></p>

                  <p><font color="#0D0000">Объект приручения </font></p>
                  <p><font color="#0D0000">Объекты приручения подразделяются на 
                    3 типа NPC: Лиса (уровень 5), Волк (уровень 15) и Хэтчлинг 
                    (Hatchling) (уровень 35). </font></p>
                  <p><font color="#0D0000">Условия для приручения </font></p>
                  <p><font color="#0D0000">Уровень игрока, который осуществляет 
                    приручение, должен быть выше, чем у приручаемого существа. 
                    Приручение не может осуществляться, если игрок уже создал 
                    сущность или уже заният приручением. </font></p>
                  <p><font color="#0D0000">Призывание прирученного существа </font></p>
                  <p><font color="#0D0000">Для того, чтобы можно было позвать 
                    прирученное существо, не должно быть уже сотворенного монстра. 
                    Питомца нельзя позвать во время боя. Его можно позвать с помощью 
                    предмета, который был использован игроком для приручения. 
                    </font></p>

                  <p><font color="#0D0000">Уход за питомцем </font></p>
                  <p><font color="#0D0000">Питомца следует кормить свежим мясом. 
                    Если держать его голодный, то он убежит. Для того, чтобы еда 
                    была под рукой в нужный момент, можно возить с собой немного 
                    мяса в сумке. Уровень питомца может расти, когда он будет 
                    приобретать опыт в сражениях. С ростом уровня он будет приобретать 
                    различные умения и возможность их использовать. Умения питомца 
                    записываются в окне умений. Игрок может управлять умениями 
                    своего питомца точно так же как своими. </font></p>
                  <p><font color="#0D0000">Управление питомцем </font></p>
                  <p><font color="#0D0000">Для того, чтобы управлять существом 
                    кликните на него двойным кликом или выберите его в окне игры. 
                    Появится интерфейс управления. Кроме того можно назначить 
                    шорткат. Возможны слудеющие команды: </font></p>
                  <p><font color="#0D0000">Рядом (Follow) Когда надато один раз, 
                    питомец бросает все свои занятия и следует за своим хозяином. 
                    Если нажать эту команду еще раз, питомец остановиться там, 
                    где находился в тот момент, и будет удерживать эту позицию. 
                    </font></p>
                  <p><font color="#0D0000">Имя (Name) Этой командой питомцу выбирается 
                    имя. Имена питомцев не должны дублироваться. Имя может состоять 
                    из 8 символов. После того, как имя присвоено, его нельзя изменить. 
                    Имя хозяина всегда пишется перед именем питомца. </font></p>

                  <p><font color="#0D0000">Атака (attak) Если выбрана эта команда, 
                    питомец будет приследовать цель своего хозяина и нападать 
                    на неё. Если атака по каким-то причинам невозможна, питомец 
                    будет дожидаться подходящего момента. Когда атака завершена, 
                    питомец вернется к своему предыдущему состоянию. Другими словами, 
                    если предыдущая команда была &quot;рядом&quot;, то питомец 
                    будет выполнять её. Если команда была &quot;сидеть&quot; (standby), 
                    он вернется на свой прежнее место и будет ждать указаний. 
                    <br>
                    Стоять (Stop) Если выбрана эта команда, питомец немедленно 
                    прекратит выполнение того, чем занят (Атака (Attak), Собирательство 
                    (Collect) и т.п.), и вернется к предыдущей выполняемой команде. 
                    </font></p>
                  <p><font color="#0D0000">Собирать (Collect) Если выбрана эта 
                    команда, питомец соберет предметы, находящеся рядом с ним, 
                    и вернется к предыдущему состоянию движения. Команда аналогична 
                    команде &quot;собирать&quot; у персонажа. </font></p>

                  <p><font color="#0D0000">Возврат (Return) Питомца можно вернуть, 
                    исользуя контроллирующий предмет. Однако, питомца нельзя вернуть, 
                    если оно занято в битве или оно голодно больше, чем на 50%. 
                    Для осуществления возврата должно пройти 5 минут с момента 
                    отправки питомца на задание. <br>
                    Снаряжение питомца </font></p>
                  <p><font color="#0D0000">Прирученный монстр может носить различные 
                    типы снаряжения, чтобы улучшить свои возомжности. Такое снаряжение 
                    разрабатывается специально для монстров. Только один тип доспеха 
                    и одно оружие могут быть надеты на монстра одновременно. Предметы 
                    также подразделяются по уровню и магическим свойствам. <br>
                    Использование инвентаря и снаряжения питомца <br>
                    Прирученный монстр обладает небольшим инвентарем, который 
                    можно использовать для сбора и хранения предметов или надеваемых 
                    вещей. <br>
                    Для того, чтобы воспользоваться чем-то из инвенторя, откройте 
                    его и нажмите двойным нажатием на предмет. Обратите внимание, 
                    что вещи не активизируются автоматически. </font></p>

                  <p><font color="#0D0000">Передача, Смерть и выпускание на волю 
                    питомца <br>
                    Питомца можно передать другому игроку. Это осуществляется 
                    при помощи &quot;контроллирующего предмета&quot;, который 
                    содержался в инвентаре до вызова питомца. </font></p>
                  <p><font color="#0D0000">Существо также может быть отпущен она 
                    волю своим хозяином. Также существо окажется на воле, если 
                    его голод дотигнет предела. Оказавшись на воле, существо оставит 
                    при себе предметы, которые находились у него. Если последней 
                    окмандой была атака, то после освобождения монстр начнет нападать 
                    на своего бывшего хозяина. </font></p>
                  <p><font color="#0D0000">Питомец может умереть в бою, после 
                    чего исчезнет в течении трех минут. Поэтому его следует оживить 
                    за эти три минуты. Но эту операцию может проделать только 
                    хозяин прирученного монстра. Когда питомец умирает, из него 
                    выпадают предметы, которые у него были. Если хозяин телепортируется, 
                    труп питомца тоже будет телепортирован.</font></p>
