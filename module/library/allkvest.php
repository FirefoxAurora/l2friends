                  <p>Он хочет, чтобы Вы собрали эти компоненты 
                    для него, но, улучшая приправу, можно улучшить качество блюда. 
                    В конце этих поисков Magister Mirien (в храме Диона) оценит 
                    блюдо и даст Вам соответствующую награду. <br>
                    1. Сок Мандрагоры (Mandragora Sap) <br>
                    Trader Sonia (маг.магазин) поможет Вам получить сок. Она даст 
                    Вам Botany Book для первых классов и скажет охотиться на Мандрагор 
                    (Mandragora) для получения их корней. Мандрагоры водятся к 
                    востоку от Dion town (на краю EG). Мандрагора дает Красный 
                    Корень Мандрагоры (Red Mandragora Root) и более редкий Белый 
                    Корень Мандрагоры (White Mandragora Root). Вы должны собрать 
                    40 красных корней, чтобы сделать Красный Сок Мандрагоры (Red 
                    Mandragora Sap), однако, если Вы соберете 40 белых корней, 
                    то она сделает Вам более качественный Белый Сок Мандрагоры 
                    (White Mandragora Sap). <br>

                    2. Мед (Honey) <br>
                    Guard Jacob (у западных ворот Диона ) поможет Вам добыть мед. 
                    Он даст Вам книжку по пчеловодству (Insect Book) и скажет 
                    Вам охотиться на Сверхъестественную Пчелу (Weird Bee или Stinger) 
                    и Кровавую Пчелу (Bloody Bee) ради нектара. Пчелы водятся 
                    в районе болота к северу от города. Кровавые Пчелы - югу от 
                    города, около первой развилки дороги. Обе пчелы могут с собой 
                    таскать: Нектар (Nectar) и более редкое Королевское Желе (Royal 
                    Jelly). Вы должны собрать 20 Nectar, чтобы сделать Мед, однако, 
                    если Вы соберете 10 Королевских Желе, то он сделает Вам более 
                    качественный Золотой Мед (Golden Honey). <br>
                    3. Пучок петрушки (Moss Bundle) <br>
                    Так как на базар идти далеко, Father Glyvka (в храме) поможет 
                    Вам достать эту петрушку. Он даст Вам учебник ботаники для 
                    вторых классов и скажет Вам охотиться на Серого Муравья (Grey 
                    Ant) и Темно-красного Муравья (Crimson Ant). Оба муравья расположены 
                    к северу от Диона, за горами. Муравьи носят с собой два вида 
                    петрушки: Зеленую Петрушку (Green Marsh Moss) и более редкую 
                    (не успевает завясть) Коричневую Петрушку (Brown Marsh Moss). 
                    Вы должны конфисковать 20 веточек петрушки для него, чтобы 
                    он смог ее обмотать ниткой и получить Пучок Зеленой Петрушки 
                    (Green Moss Bundle), однако, если Вам удастся достать 20 веточек 
                    завядшей петрушки, то он сделает Вам более качественный Пучок 
