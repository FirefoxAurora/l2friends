<div id="post_message_78"><u><b>Orc Raider</b></u> - Квест начинается с 19 уровня. Рекомендуем пройти его после получения 20 уровня.<br>
<br>
01. Говорим с Prefect Karukia(Orc Village). Orc Raider должен доказать
что он не боится даже смерти. Она даст Goblin Dwelling Map(1).<br>
02. Убиваем Goblin Grave Robbers. Собираем Green Blood пока не наберём
25. Когда наберётся 25 единиц Gdreen Blood'а кровь исчезнет и появится
Karuka Ratman Leader. Убиваем его и получаем Ratman Teeth. Повторяем
эту прочедуру пока не наберем 10 Ratman Teeth.<br>
03. Относим Ratman Teeth  к Karuk'е. Она даст 2 Betrayer Reports. <br>
Теперь пришло время доказать свою преданность оркам.Нужно найти двух
предателей "Orc Betrayers" (любых 2 из списка - см.ниже) и убить их.
После убийства каждого предателя у вас окажется его голова. Их
местоположение: сначала идут координаты по карте , а потом точные
координаты полученые командой /loc<br>
