<br>
В пещере под холмом убиваем сначала телохранителей Cave Basilisk, а затем и саму Baraham, получаем Watchers Eye2.<br>
<br>
Появится Raldo, разговариваем с ним.<br>
<br>
Если Raldo все же не появился, то продолжаем убивать Cave Basilisk,
пока не появится Baraham, затем убиваем и его. Процедуру придется
повторять, пока не появится Raldo.<br>
