<u><b>Paagrio Monk </b></u>- Квест начинается с 19 уровня <br>
<br>
01. Говорим с Gantaki Zu Urutu(Orc Village). Нужно получить scroll'ы от
двух мастеров чтобы стать монахом. Берём Pomegranate gift.<br>
02. Говорим с Khavatari Roshek (находится в Frozen WaterFalls) и даём
ему Pomegranate. Он даст нам Leather Pouch и отправит собирать 5 Kasha
Bear Claws(бегают рядом с ним). Вы можете использовать только голые
руки или Dual-Fist оружие. Один за другим он даст 3 Leather Pouches
которые вы должны вернуть полными трофеев.<br>
03. Убиваем Kasha Bears пока не соберём 5 Claws и Leather Pouch не превратится в Leather Pouch 1 (Full). Возвращаемся к Rosheek.<br>
04. Дальше валим Kasha Blade Spider(идти на северо-восток от
WaterFalls, через мост) и собираем 5 Kasha Blade Spider Talons.
Возвращаемся к Rosheek'у.<br>
05. Идём наполнять третий Pouch. Его наполнят Scarlet
Salamanders(находятся в Cave of Trials) своими Scarlet Salamanders
Scales. Возвращаемся к Rosheek'у и берём у него Fiery Spirit Scroll и
Rosheek's Letter.<br>
06. Несём письмо к Gantaki Zu Urutu(Orc Village). Меняем на Gantaki's Letter of Recommendation.<br>
07. Общаемся с Prefet Kasman(Gludin). Получаем Fit gift.<br>
08. Идём к Khavatari Toruku(40.2-51 дорога на Север из Глудина, он
будет у развилки с Orc Barracks/Ruins of Agony дорогой). Он даст
Leather Purse который нужно наполнить Токенами.<br>
09. Всего нужно 12 Токенов. По 3 каждого: Vuku Orc Fighter
(Tusk)(39.5-51.5), Ratman Warrior (Fang)(40-55), Langk Lizardmen
Warrior (Tooth)(40-56) и Felim Lizardman Warrior (Tooth)(40-56).<br>
10. Отдаём полный Pouch Toruku. Получаем Iron Will Scroll и Toruku's Letter.<br>
11. Говорим с Kasman'ом. Он поменяет Scroll'ы и letter на Khavatori Totem.<br>
12. Идём к High Prefect Obsum(Gludin) и становимся Paagrio Monk'ом.<br>
