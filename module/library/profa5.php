<br>
Ищем Gray Pillar's Filaur, разговариваем с ним.<br>
Телепортируемся к North Coast. Бежим в Mithril Mines. Бежим в самую
глубь, пока не добежим до огромной круглой пещеры, добираемся до ее
дна, и выбегаем в огромную пещеру (там есть второй выход, но если Вы
случайно свернете в него, то все равно попадете в круглую пещеру). Из
огромной пещеры выбираемся наверх. Бежим по мостам, пока не доберемся
до подземного острова.<br>
<br>
Убиваем сначала телохранителя, а затем и саму Queen of Succubus.<br>
<br>
Появится Raldo, разговариваем с ним, получаем Mark of Challenger.<br>
<br>
Если Raldo все же не появился, то продолжаем убивать сначала
телохранителя, а затем и саму Queen of Succubus, пока не появится Raldo.<br>
<br>
Изредка Queen of Succubus и ее телохранитель падают с острова вниз в
воду. Если Вы обнаружите, что это так - прыгайте за ними, убейте их, а
затем бегите в огромную пещеру и назад на остров.<br>
