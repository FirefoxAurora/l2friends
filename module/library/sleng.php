<p>С каждым днем все больше и больше в нашем мире появляется новых слов, поэтому невозможно уместить все их в одну статью. Мой вам совет и наставление: Эти слова придумывают люди, а так как вы тоже человек вы можете понять смысл и первопричину их появления. Для примера локация РоД – переводится как Ruins of Despair(взяты первые три буквы), или оружие СоР (Sword of Revolution).