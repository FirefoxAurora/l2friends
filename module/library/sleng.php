                <h2>Частичный слэнг мира Лайнэйдж.</h2>
                В данной статье я постараюсь коснуться одной из важных проблем нашего фантазийного мира, а если быть точнее “проблемы языка”.<br />
Не раз многие из вас сталкивались с такой ситуацией, что встретив человека, предположим в каком-либо из пабов Диона, вы не могли разобрать и слова, что вылетали из его уст.<br />
Как и полагается в начале, я расскажу о формах приветствия:<br />
<p></p>
<table>
  <tr>
    <td><b>Hello</b></td>
    <td>– данное слово в переводе с неизвестного языка означает привет.</td>
  </tr>
  <tr>
    <td><b>Hi</b></td>
    <td>– как и hello означает тоже самое, но используется населением чаще, по причине своей компактности и стильности произношения.</td>
  </tr>
  <tr>
    <td><b>Hey</b></td>
    <td>– используется как приветствие или как оклик.</td>
  </tr>
</table>
<p></p>
В основном именно от этих слов уже идут другие приветствия, но более измененные под себя. Для примера: хай, хаюшки и т.д. Стоит так же выделить 4 группу, но они бытуют в нашем языке уже с древности(здарова, привет, прю и т.д.)<br />
Теперь коснемся формы прощания:<br />
<p></p>
<table>
  <tr>
    <td><b>Бай</b></td>
    <td>– часто используемое людьми слово для церемонии прощания.</td>
  </tr>
  <tr>
    <td><b>bye</b></td>
    <td>– по сути своей тоже самое, что и бай, но лишь с различием языка.</td>
  </tr>
  <tr>
    <td><b>bb</b></td>
    <td>– сокращенно от bye bye, самая популярная форма прощания.</td>
  </tr>
  <tr>
    <td><b> Пока</b></td>
    <td>– комментарии излишни. Стоит только учесть, что это слово так же изменяемо(покушки, поки , пуки , пок).</td>
  </tr>
</table>
